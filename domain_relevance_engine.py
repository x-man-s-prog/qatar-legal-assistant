# (§1) تطبيع النص العربي — نسخة خفيفة مستقلة
# ══════════════════════════════════════════════════════════════════════════
_CHAR_MAP   = str.maketrans('أإآٱؤئةى', 'ااااويها')
_DIACRITICS = re.compile(r'[\u064B-\u065F\u0670\u0640]')
_PREFIX_RE  = re.compile(r'^(وال|فال|بال|كال|ال|و|ف|ب|ك)')

def _norm(text: str) -> str:
    """تطبيع عربي سريع: توحيد الألف، حذف التشكيل"""
    return _DIACRITICS.sub('', text.translate(_CHAR_MAP)).strip()

def _root(word: str) -> str:
    """جذر مبسَّط: احذف البادئات الشائعة"""
    n = _norm(word)
    return _PREFIX_RE.sub('', n) if len(n) > 4 else n


