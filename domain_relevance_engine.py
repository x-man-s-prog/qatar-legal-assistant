# (§8) الواجهة الرئيسية — DomainRelevanceEngine
# ══════════════════════════════════════════════════════════════════════════
class DomainRelevanceEngine:
    """
    محرك الصلة القانونية — الواجهة الرئيسية للتكامل مع النظام.

    الاستخدام:
        engine = DomainRelevanceEngine()
        ranked = engine.rerank(q, chunks, extra_terms=legal_terms_from_qe)
    """

    def __init__(self):
        # إحصاءات الأداء
        self._calls     = 0
        self._total_ms  = 0.0
        log.info("DomainRelevanceEngine loaded — weights: BASE=%.2f DOMAIN=%.2f "
                 "PRIORITY=%.2f CONCEPT=%.2f YEAR=%.2f",
                 W_BASE, W_DOMAIN, W_PRIORITY, W_CONCEPT, W_YEAR)

    def rerank(
        self,
        q: str,
        chunks: list[dict],
        extra_terms: list[str] | None = None,
        domain_hint: str | None = None,   # من CoT إذا توفّر
    ) -> list[dict]:
        """
        يُعيد ترتيب قائمة المقاطع بالمعادلة الجديدة.

        المدخلات:
            q           — السؤال الأصلي
            chunks      — قائمة المقاطع (كل chunk قاموس يحتوي score, law_name, ...)
            extra_terms — مصطلحات قانونية إضافية من query_engine
            domain_hint — مجال من CoT/query_engine (يُعطى أولوية على التصنيف التلقائي)

        المخرجات:
            قائمة مُعاد ترتيبها + حقل "dre_score" مضاف لكل chunk
        """
        if not chunks:
            return chunks

        t0 = time.monotonic()
        self._calls += 1

        # (1) صنّف المجال
        if domain_hint:
            domain, domain_conf = domain_hint, 0.90
        else:
            domain, domain_conf = classify_query_domain(q, extra_terms)

        log.debug("DRE: domain=%s conf=%.2f chunks=%d", domain, domain_conf, len(chunks))

        # (2) احسب مكافأة التجمّع
        cluster_bonus = _compute_law_cluster_bonus(chunks)

        # (3) سجّل كل chunk
        scored = []
        for ch in chunks:
            dre_score = _score_single_chunk(ch, domain, domain_conf, cluster_bonus)
            new_ch = dict(ch)         # لا تُعدّل الأصل
            new_ch["dre_score"]   = dre_score
            new_ch["dre_domain"]  = domain or ""
            new_ch["score"]       = dre_score   # استبدل درجة الاسترجاع الأصلية
            scored.append(new_ch)

        # (4) رتّب تنازلياً
        scored.sort(key=lambda x: x["dre_score"], reverse=True)

        elapsed = (time.monotonic() - t0) * 1000
        self._total_ms += elapsed
        avg_ms = self._total_ms / self._calls

        log.info(
            "DRE rerank: domain=%s conf=%.2f chunks=%d → top=%s (%.3f) | %.1fms (avg:%.1fms)",
            domain, domain_conf, len(chunks),
            scored[0].get("law_name", "?")[:40] if scored else "?",
            scored[0].get("dre_score", 0) if scored else 0,
            elapsed, avg_ms
        )

        return scored

    def get_stats(self) -> dict:
        """إحصاءات الأداء"""
        return {
            "calls":     self._calls,
            "avg_ms":    round(self._total_ms / max(self._calls, 1), 2),
            "total_ms":  round(self._total_ms, 2),
        }


