def classify_query_domain(q: str,
                           extra_terms: list[str] | None = None
                           ) -> tuple[str | None, float]:
    """
    يُصنّف الاستعلام إلى مجال قانوني محدد.

    المدخلات:
        q            — نص الاستعلام
        extra_terms  — مصطلحات إضافية من query_engine (اختياري)

    المخرجات:
        (domain, confidence) — المجال + درجة الثقة (0.0–1.0)
        domain = None إذا لم يُكتشف مجال واضح

    الخوارزمية:
        لكل مجال: score = (kw_hits/total_kws × 0.7) + (phrase_hits × 0.3)
        يُعاد المجال الأعلى إذا كانت ثقته >= 0.15 (عتبة منخفضة لتغطية أوسع)
    """
    q_norm = _norm(q.lower())
    # احذف البادئات من كل كلمة للمطابقة الجذرية
    # أزل علامات الترقيم (؟ ! ، .) من الكلمات قبل استخلاص الجذر
    _PUNCT = re.compile(r'[؟?!،,.\-_]')
    q_roots = {_root(_PUNCT.sub('', w)) for w in q_norm.split()
               if len(_PUNCT.sub('', w)) >= 3}

    # ══ إشارات قوية حصرية — تُعطي مجالاً معيّناً أولوية قصوى ══
    # هذه مصطلحات لا تظهر إلا في مجال واحد → تتجاوز تأثير extra_terms
    _q_full_early = _norm(q)
    _STRONG_SIGNALS: list[tuple[str, str]] = [
        # (مصطلح حصري، المجال)
        ("تقاعد",     "administrative"),   # التقاعد = حصري للإداري
        ("احاله للتقاعد", "administrative"),
        ("إحالة للتقاعد", "administrative"),
        ("سن التقاعد",    "administrative"),
        ("خدمة مدنية",    "administrative"),
        ("الخدمة المدنية","administrative"),
        ("مكافاة نهايه الخدمه", "labor"),  # مكافأة نهاية الخدمة = عمالي بامتياز
        ("فصل تعسفي",     "labor"),
        ("قانون العمل",   "labor"),
        # ── إضافات v1.2 ──
        ("شيك بدون رصيد",  "criminal"),     # الشيك المرتجع فيه عقوبة جنائية (ق.ع. م.357)
        ("شيك بلا رصيد",   "criminal"),
        ("شيك مرتجع",      "criminal"),
        ("شيك",            "civil"),       # الشيك العام = مدني/تجاري (دون تحديد العقوبة)
        ("ايجار",          "property"),    # إيجار = عقاري دائماً
        ("عقد ايجار",      "property"),
        ("إخلاء",          "property"),    # إخلاء المستأجر = عقاري
        ("حضانة",          "family"),      # حضانة = أسرة
        ("نفقة",           "family"),      # نفقة = أسرة
        ("ميراث",          "family"),      # ميراث = أسرة
        ("توزيع التركة",   "family"),
        ("الإرث",          "family"),
        ("جرائم إلكترونية","cyber"),
        ("ابتزاز إلكتروني","cyber"),
        ("نشر صور",        "cyber"),
        # ── إهمال جنائي وحوادث الوفاة ──
        ("بسبب الإهمال",   "criminal"),   # يُحدد السياق الجنائي للحادث
        ("تسبب في وفاة",   "criminal"),
        ("أدى لوفاة",      "criminal"),
        ("قتل خطأ",        "criminal"),
        ("القتل الخطأ",    "criminal"),
        ("إهمال جنائي",    "criminal"),
        # ── رد الاعتبار — إجرائي/جنائي ──
        ("رد اعتبار",      "procedural"),  # رد اعتباري، رد اعتبار
        ("رد الاعتبار",    "procedural"),
        ("إعادة الاعتبار", "procedural"),
        # ── مخالفة الإقامة ──
        ("انتهت إقامتي",   "administrative"),
        ("انتهاء الإقامة", "administrative"),
        ("مخالفة الإقامة", "administrative"),
    ]
    for signal_kw, signal_domain in _STRONG_SIGNALS:
        sig_norm = _norm(signal_kw)
        if sig_norm in _q_full_early:
            # هذا المجال له أولوية قصوى — أعده فوراً بثقة عالية
            log.debug("strong_signal: q='%.30s' → %s (signal=%s)", q, signal_domain, signal_kw)
            return signal_domain, 0.95

    # أضف extra_terms إذا وُجدت
    if extra_terms:
        for t in extra_terms:
            for w in _norm(t).split():
                w_clean = _PUNCT.sub('', w)
                if len(w_clean) >= 3:
                    q_roots.add(_root(w_clean))

    # طبّق نفس التطبيع على q كاملاً للبحث عن العبارات — مع مسافات حدودية
    # السبب: منع مطابقة "اجر" داخل "الاجراءات" (خطأ substring)
    q_full_norm   = _norm(q)
    # نسخة ذات مسافات حدودية للمطابقة الدقيقة للكلمات المفردة القصيرة
    q_full_spaced = " " + q_full_norm + " "

    domain_scores: dict[str, float] = {}

    for domain, info in _DOMAIN_KEYWORDS.items():
        kw_set = info["kws"]
        phrases = info.get("phrases", set())

        # (a) مطابقة كلمات مفتاحية (جذرية)
        kw_hits = 0
        for kw in kw_set:
            kw_root = _root(kw)
            # مطابقة q_roots: كلمات مستخرجة بشكل نظيف → مطابقة دقيقة
            if kw_root in q_roots:
                kw_hits += 1
                continue
            # مطابقة q_full_norm: للعبارات المركبة (أكثر من كلمة)
            # نستخدم المسافات الحدودية فقط للكلمات القصيرة (≤4 أحرف) لمنع false positives
            if " " in kw_root:
                # عبارة مركبة: substring matching مقبول
                if kw_root in q_full_norm:
                    kw_hits += 1
            elif len(kw_root) >= 3:
                # كلمة 3+ أحرف: substring matching آمن للجذور العربية
                # مثال: "زوج" يتطابق صحيحاً داخل "زوجتين" (صيغة المثنى)
                # ملاحظة: "اجر" أُزيلت من قوائم الكلمات لأنها تتعارض مع "الإجراءات"
                if kw_root in q_full_norm:
                    kw_hits += 1
            # ملاحظة: كلمات 1-2 حرف لا تُفحص (احتمال false positive عالٍ)

        # نسبة التغطية (ليس عدداً مطلقاً — لتجنب تفضيل المجالات الأكبر)
        kw_ratio = min(1.0, kw_hits / max(5, len(kw_set) * 0.15))

        # (b) مطابقة عبارات مركبة (وزن مضاعف)
        phrase_bonus = 0.0
        for ph in phrases:
            ph_norm = _norm(ph)
            if ph_norm in q_full_norm:
                phrase_bonus = min(0.5, phrase_bonus + 0.25)

        # درجة المجال النهائية
        raw_score = kw_ratio * 0.70 + phrase_bonus * 0.30
        # ضرب في وزن المجال (يُقلّل المجالات الأقل أهمية)
        domain_scores[domain] = raw_score * info["weight"]

    if not domain_scores:
        return None, 0.0

    best_domain = max(domain_scores, key=lambda d: domain_scores[d])
    best_score  = domain_scores[best_domain]

    # عتبة الثقة: 0.10 كافية للتصنيف
    if best_score < 0.10:
        return None, best_score

    log.debug("domain_classify: q='%.30s' → %s (%.2f)", q, best_domain, best_score)
    return best_domain, min(1.0, best_score)


