# (§5) عامل السنة المُقيَّد — Bounded Year Factor
# ══════════════════════════════════════════════════════════════════════════
# المشكلة القديمة: year_boost يصل حتى +0.03 على نتائج 0.62–0.97
# في النظام الجديد: year_factor ∈ [−0.05, +0.05] — مقيّد دائماً
# وزنه في المعادلة النهائية: 0.05 فقط → تأثير فعلي = ±0.0025
# ══════════════════════════════════════════════════════════════════════════
def _year_factor(law_year: str) -> float:
    """
    عامل السنة المُقيَّد — [−1.0, +1.0] يُضرب في 0.05 لاحقاً.

    جدول:
        ≥ 2020 → +1.0  (حديث جداً)
        ≥ 2010 → +0.5  (حديث)
        ≥ 2000 → +0.0  (محايد — الحقبة الذهبية لمعظم القوانين القطرية)
        ≥ 1990 → -0.5  (قديم نسبياً)
        < 1990 → -1.0  (قديم)
        لا سنة → +0.0  (محايد — لا عقوبة ولا مكافأة)

    ملاحظة مهمة:
        قانون العقوبات 2004 → year_factor = +0.0 (محايد) → لا عقوبة
        قانون 2024 غير مرتبط → year_factor = +1.0 → تأثير = +0.05 فقط
        لن يتجاوز هذا الـ domain_match + law_priority (0.25+0.20 = 0.45)
    """
    try:
        y = int(str(law_year).strip()) if law_year else 0
        if y <= 0:    return 0.0   # لا سنة → محايد تماماً
        if y >= 2020: return 1.0
        if y >= 2010: return 0.5
        if y >= 2000: return 0.0
        if y >= 1990: return -0.5
        return -1.0
    except (ValueError, TypeError):
        return 0.0


