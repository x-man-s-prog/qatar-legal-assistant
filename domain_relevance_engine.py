# (§7) المعادلة النهائية للتسجيل — Final Score Formula
# ══════════════════════════════════════════════════════════════════════════
#
# score_final = (
#     base_score       × 0.35   ← درجة الاسترجاع الأصلية
#     + domain_match   × 0.25   ← هل مجال القانون يتطابق؟
#     + law_priority   × 0.20   ← هل هذا القانون الحاكم؟
#     + concept_cov    × 0.15   ← هل المفاهيم الجوهرية موجودة؟
#     + year_factor    × 0.05   ← عامل السنة (مقيَّد)
# )
# + cluster_bonus              ← تجمّع المواد من نفس القانون
#
# الحد الأقصى للدرجة: 1.0 (مقطوعة)
# ══════════════════════════════════════════════════════════════════════════

# أوزان المعادلة — قابلة للضبط
W_BASE      = 0.35
W_DOMAIN    = 0.25
W_PRIORITY  = 0.20
W_CONCEPT   = 0.15
W_YEAR      = 0.05   # أقصى تأثير حقيقي = 0.05 × 1.0 = 0.05

def _score_single_chunk(
    ch: dict,
    domain: str | None,
    domain_confidence: float,
    cluster_bonus: dict[str, float],
) -> float:
    """
    يُطبّق المعادلة الكاملة على chunk واحد.

    المدخلات:
        ch                — قاموس الـ chunk (يحتوي: score, law_name, law_year, content)
        domain            — المجال المصنَّف للاستعلام
        domain_confidence — ثقة التصنيف (تُضعّف domain_match عند عدم اليقين)
        cluster_bonus     — مكافأة التجمّع المحسوبة مسبقاً

    المخرجات:
        درجة نهائية [0.0, 1.0]
    """
    base_score = float(ch.get("score", 0.0))
    law_name   = ch.get("law_name", "")
    law_year   = str(ch.get("law_year", "") or "")
    content    = ch.get("content", "")

    # ── (a) Domain Match ──
    # هل يُذكر مجال الاستعلام في اسم القانون أو المحتوى؟
    domain_match = 0.0
    if domain:
        law_norm_text = _norm((law_name + " " + content[:300]).lower())
        domain_info   = _DOMAIN_KEYWORDS.get(domain, {})
        domain_kws    = domain_info.get("kws", set())
        domain_phrs   = domain_info.get("phrases", set())

        kw_hits = sum(1 for kw in domain_kws if _root(kw) in law_norm_text)
        kw_ratio = min(1.0, kw_hits / max(3, len(domain_kws) * 0.10))

        phrase_hit = any(_norm(ph.lower()) in law_norm_text for ph in domain_phrs)
        domain_match = kw_ratio * 0.6 + (0.4 if phrase_hit else 0.0)
        # اضرب بثقة التصنيف لتخفيف التأثير عند عدم اليقين
        domain_match *= domain_confidence

    # ── (b) Law Priority ──
    law_priority = _get_law_priority_score(law_name, domain or "")

    # ── (c) Concept Coverage ──
    concept_cov = _get_concept_coverage(content, law_name, domain or "")

    # ── (d) Year Factor ──
    yf = _year_factor(law_year)

    # ── (e) Cluster Bonus ──
    law_key = _norm(law_name.lower())[:40]
    cluster = cluster_bonus.get(law_key, 0.0)

    # ── المعادلة النهائية ──
    score = (
        base_score  * W_BASE    +
        domain_match * W_DOMAIN +
        law_priority * W_PRIORITY +
        concept_cov  * W_CONCEPT +
        yf           * W_YEAR
    ) + cluster

    return round(min(1.0, max(0.0, score)), 6)


