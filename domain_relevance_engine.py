def _get_law_priority_score(law_name: str, domain: str) -> float:
    """
    يُحدد أولوية القانون في مجاله.

    المنطق:
        1. طبّق _norm على اسم القانون
        2. ابحث عن تطابق جزئي مع قائمة الأولويات
        3. أعد أعلى درجة تطابق

    مثال:
        قانون رقم 11 لسنة 2004 بإصدار قانون العقوبات → يتطابق مع "قانون العقوبات" → 1.00
    """
    if not law_name or not domain:
        return 0.0

    priorities = _LAW_PRIORITY.get(domain, [])
    if not priorities:
        return 0.0

    law_norm = _norm(law_name.lower())
    best = 0.0

    for pattern, weight in priorities:
        pat_norm = _norm(pattern.lower())
        # مطابقة جزئية: هل النمط موجود في اسم القانون؟
        if pat_norm in law_norm:
            best = max(best, weight)

    return best


