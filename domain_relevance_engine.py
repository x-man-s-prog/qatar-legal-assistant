# (§6) تعزيز التجمّع — Cross-Chunk Reinforcement
# ══════════════════════════════════════════════════════════════════════════
def _compute_law_cluster_bonus(chunks: list[dict]) -> dict[str, float]:
    """
    يُحدد مكافأة التجمّع: عندما يأتي أكثر من chunk من نفس القانون
    → كل chunk يحصل على مكافأة تدريجية (إشارة على أن هذا القانون مرتبط فعلاً).

    الصيغة:
        bonus(law) = min(0.08, (count - 1) × 0.025)
        count=1 → 0.00 (لا مكافأة)
        count=2 → 0.025
        count=3 → 0.050
        count=4 → 0.075
        count≥5 → 0.08 (حد أقصى)

    لماذا؟
        قانون العقوبات قد يُعطي 4 مواد لسؤال عقوبة السرقة →
        هذا دليل على أن النظام وجد تغطية واسعة → يستحق تعزيزاً.
    """
    # عدّ chunks لكل قانون
    law_counter: dict[str, int] = {}
    for ch in chunks:
        law_key = _norm(ch.get("law_name", "").lower())[:40]
        law_counter[law_key] = law_counter.get(law_key, 0) + 1

    # احسب المكافأة لكل قانون
    bonuses: dict[str, float] = {}
    for law_key, count in law_counter.items():
        bonuses[law_key] = min(0.08, (count - 1) * 0.025)

    return bonuses


