def _get_concept_coverage(content: str, law_name: str, domain: str) -> float:
    """
    يقيس مدى وجود المفاهيم القانونية الجوهرية للمجال في المحتوى.

    الصيغة:
        coverage = hits / min(len(concepts), 5)   ← أقصاه 5 لتسريع الحساب
    """
    if not domain:
        return 0.0

    concepts = _DOMAIN_CONCEPTS.get(domain, [])
    if not concepts:
        return 0.0

    text_norm = _norm((content + " " + law_name).lower())
    hits = sum(1 for c in concepts if _norm(c.lower()) in text_norm)
    return min(1.0, hits / min(len(concepts), 5))


