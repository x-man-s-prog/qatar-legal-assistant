def get_all_synonyms(query: str) -> list[str]:
    """
    يُعيد جميع المرادفات للمصطلحات القانونية الموجودة في الاستعلام.
    مفيد لتوسيع الكلمات المفتاحية في البحث.
    """
    extras: list[str] = []
    for term, synonyms in LEGAL_SYNONYMS.items():
        if term in query:
            extras.extend(synonyms[:2])   # أخذ المرادفَين الأولَين فقط
    return list(dict.fromkeys(extras))    # إزالة التكرار مع الحفاظ على الترتيب
