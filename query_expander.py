# expand
# ══════════════════════════════════════════════════════════════
def expand(query: str) -> list[str]:
    """
    يُولّد ثلاث صياغات مختلفة للاستعلام القانوني.

    الاستراتيجيات:
      1. استبدال المصطلحات بمرادفاتها الأولى
      2. صياغة السؤال بأسلوب القانون الرسمي (مادة + قانون + سنة)
      3. صياغة وصفية (ماذا يحدث إذا؟ / ما العقوبة؟)

    تُعيد دائماً 3 عناصر (قد تتكرر إذا لم يُوجد بديل).
    """
    q = query.strip()
    if not q:
        return [q, q, q]

    entities = extract_legal_entities(q)
    variants: list[str] = []

    # ── الصياغة 1: استبدال المصطلحات بمرادفاتها ──
    v1 = _replace_with_synonyms(q)
    variants.append(v1)

    # ── الصياغة 2: صياغة قانونية رسمية ──
    v2 = _build_formal_query(q, entities)
    variants.append(v2)

    # ── الصياغة 3: صياغة وصفية/تفسيرية ──
    v3 = _build_descriptive_query(q, entities)
    variants.append(v3)

    return variants


def _replace_with_synonyms(query: str) -> str:
    """يستبدل أول مصطلح معروف بمرادفه الأول في القاموس."""
    for term, synonyms in LEGAL_SYNONYMS.items():
        if term in query and synonyms:
            return query.replace(term, synonyms[0], 1)
    return query


def _build_formal_query(query: str, entities: dict) -> str:
    """يبني صياغة قانونية رسمية تشمل الكيانات المستخرجة."""
    parts = []

    # أضف نوع القانون إن وُجد
    law_type = entities.get("law_type")
    if law_type:
        parts.append(f"في {law_type}")

    # أضف رقم القانون إن وُجد
    law_number = entities.get("law_number")
    year       = entities.get("year")
    if law_number and year:
        parts.append(f"قانون رقم {law_number} لسنة {year}")
    elif law_number:
        parts.append(f"قانون رقم {law_number}")

    # أضف رقم المادة إن وُجد
    article = entities.get("article")
    if article:
        parts.append(f"المادة {article}")

    if parts:
        return f"{query} — " + "، ".join(parts)
    return query   # لا كيانات → الاستعلام الأصلي


def _build_descriptive_query(query: str, entities: dict) -> str:
    """يبني صياغة وصفية تفسيرية للاستعلام."""
    # إضافة سياق وصفي بناءً على نوع القانون
    law_type = entities.get("law_type")
    if law_type == "عقوبات":
        return f"ما النص القانوني والعقوبة المقررة — {query}"
    elif law_type == "عمل":
        return f"ما حقوق العامل وحماية قانون العمل — {query}"
    elif law_type == "أسرة":
        return f"ما الحكم الشرعي والقانوني — {query}"
    elif law_type == "مدني":
        return f"ما الالتزامات والمسؤولية المدنية — {query}"
    elif law_type == "إجراءات":
        return f"ما الخطوات القانونية والإجراءات — {query}"

    # كلمات إشارية في السؤال
    q_lower = query.lower()
    if any(w in q_lower for w in ("عقوبة", "جزاء", "غرامة", "سجن")):
        return f"ما النص القانوني الصريح والعقوبة — {query}"
    if any(w in q_lower for w in ("حق", "حقوق", "يحق", "مسموح")):
        return f"ما الحقوق القانونية المكفولة — {query}"
    if any(w in q_lower for w in ("إجراء", "خطوة", "كيف", "طريقة")):
        return f"ما الإجراءات والخطوات القانونية — {query}"

    return f"اشرح الحكم القانوني الدقيق — {query}"


