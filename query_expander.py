# أنماط استخراج الكيانات القانونية
# ══════════════════════════════════════════════════════════════
_ARTICLE_RE = re.compile(
    r"(?:المادة|مادة|م\.?)\s*[(\[]?\s*(\d+)\s*[)\]]?",
    re.UNICODE,
)
_LAW_NUM_RE = re.compile(
    r"(?:قانون|مرسوم|لائحة|قرار)\s+رقم\s*[(\[]?\s*(\d+)\s*[)\]]?",
    re.UNICODE,
)
_YEAR_RE = re.compile(
    r"(?:لسنة|عام|سنة)\s*[(\[]?\s*((?:19|20)\d{2})\s*[)\]]?",
    re.UNICODE,
)

# كلمات مفتاحية لتحديد نوع القانون
_LAW_TYPE_PATTERNS: list[tuple[str, str]] = [
    (r"عقوبات",        "عقوبات"),
    (r"عمل",           "عمل"),
    (r"أسرة|اسره|أحوال شخصية", "أسرة"),
    (r"مدني",          "مدني"),
    (r"تجاري",         "تجاري"),
    (r"إجراءات|مرافعات", "إجراءات"),
    (r"جزائي|جنائي",  "جزائي"),
    (r"إداري",         "إداري"),
    (r"مرور|سير",      "مرور"),
    (r"إقامة|أجانب",  "إقامة"),
    (r"جنسية",         "جنسية"),
    (r"عقارات|ملكية عقارية", "عقارات"),
    (r"ضريبة|ضرائب",  "ضريبة"),
    (r"بيئة",          "بيئة"),
    (r"صحة",           "صحة"),
    (r"تعليم",         "تعليم"),
    (r"انتخاب",        "انتخابات"),
    (r"إلكترونية|جرائم إلكترونية", "إلكتروني"),
    (r"مخدرات",        "مخدرات"),
    (r"إرهاب",         "إرهاب"),
]
_LAW_TYPE_RE = [(re.compile(p, re.UNICODE | re.IGNORECASE), t) for p, t in _LAW_TYPE_PATTERNS]


# ══════════════════════════════════════════════════════════════
# extract_legal_entities
# ══════════════════════════════════════════════════════════════
def extract_legal_entities(query: str) -> dict:
    """
    يستخرج من نص الاستعلام:
      - article    : رقم المادة  (str أو None)
      - law_number : رقم القانون (str أو None)
      - year       : السنة       (str أو None)
      - law_type   : نوع القانون كلمةً (str أو None)

    أمثلة:
      "المادة 357 من قانون رقم 11 لسنة 2004"
      → {"article":"357","law_number":"11","year":"2004","law_type":"عقوبات"}

      "عقوبة السرقة في قانون العقوبات"
      → {"article":None,"law_number":None,"year":None,"law_type":"عقوبات"}
    """
    article    = _first_match(_ARTICLE_RE, query)
    law_number = _first_match(_LAW_NUM_RE, query)
    year       = _first_match(_YEAR_RE,    query)

    law_type: Optional[str] = None
    for pattern, name in _LAW_TYPE_RE:
        if pattern.search(query):
            law_type = name
            break

    return {
        "article":    article,
        "law_number": law_number,
        "year":       year,
        "law_type":   law_type,
    }


def _first_match(pattern: re.Pattern, text: str) -> Optional[str]:
    m = pattern.search(text)
    return m.group(1) if m else None


