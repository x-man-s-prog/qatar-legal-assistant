def think_as_opponent(query: str, topic: Optional[str], user_facts: dict) -> str:
    """
    يلعب دور الخصم ويُعيد سياق الهجمات المتوقعة + الردود المسبقة.
    """
    if not topic or topic not in _OPPONENT_ATTACKS:
        return ""

    confirmed = " ".join(str(x) for x in user_facts.get("confirmed", []))
    q_low = (query or "").lower()
    all_text = confirmed + " " + q_low

    attacks_found = []
    topic_data = _OPPONENT_ATTACKS[topic]

    for scenario_name, scenario_data in topic_data.items():
        triggers = scenario_data["triggers"]
        # "always" scenarios or keyword-matched scenarios
        if not triggers or any(t in all_text for t in triggers):
            for item in scenario_data["attacks"]:
                attacks_found.append(item)

    if not attacks_found:
        return ""

    ctx = "\n═══ توقّع هجمات الخصم — سُدّ الثغرات في المذكرة ═══\n"
    for i, item in enumerate(attacks_found[:5], 1):
        ctx += f"\n⚔️ هجمة {i}: {item['attack']}\n"
        ctx += f"🛡️ ردّك: {item['counter']}\n"
    ctx += "\n⚡ ابنِ دفاعك بحيث يسدّ هذه الثغرات مسبقاً — لا تنتظر الخصم يثيرها.\n"
    return ctx
