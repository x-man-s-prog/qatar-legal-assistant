def _parse_ref(ref: str) -> tuple[str, str, str, str]:
    """
    يحلل مرجع مثل 'م308_عقوبات_11_2004' إلى (رقم_مادة, اسم_قانون, رقم_قانون, سنة).
    """
    parts = ref.replace("م", "").split("_")
    article_num = parts[0] if parts else ""
    law_name = parts[1] if len(parts) > 1 else ""
    law_num = parts[2] if len(parts) > 2 else ""
    law_year = parts[3] if len(parts) > 3 else ""
    return article_num, law_name, law_num, law_year


async def fetch_article_text_from_db(ref: str, pool) -> str:
    """
    يسحب النص الفعلي للمادة من DB باستخدام regex دقيق.
    الـ DB يخزن المواد كـ 'المادة\\n304\\n...' أو 'المادة 304'.
    نستخدم regex: المادة[\\s\\n]+{num}([^0-9]|$) لتجنب المطابقة الخاطئة.
    """
    if ref in _article_cache:
        return _article_cache[ref]

    article_num, law_alias, law_num, law_year = _parse_ref(ref)
    if not article_num:
        return ""

    law_pattern = _LAW_NAME_MAP.get(law_alias, f"%{law_alias}%") if law_alias else None
    # regex: المادة ثم مسافة/سطر جديد ثم الرقم بالضبط ثم غير رقم
    regex_pattern = f"المادة[\\s\\n]+{article_num}([^0-9]|$)"

    try:
        async with pool.acquire() as conn:
            # ─── بحث 1: regex دقيق + law_name ───
            if law_pattern:
                row = await conn.fetchrow(
                    """SELECT content FROM chunks
                       WHERE is_active = true
                       AND law_name ILIKE $2
                       AND content ~ $1
                       ORDER BY length(content) ASC
                       LIMIT 1""",
                    regex_pattern,
                    law_pattern,
                )
                if row and len(row["content"]) > 30:
                    text = row["content"][:800]
                    _article_cache[ref] = text
                    return text

            # ─── بحث 2: regex فقط بدون law_name (أوسع) ───
            row = await conn.fetchrow(
                """SELECT content FROM chunks
                   WHERE is_active = true
                   AND content ~ $1
                   ORDER BY length(content) ASC
                   LIMIT 1""",
                regex_pattern,
            )
            if row and len(row["content"]) > 30:
                text = row["content"][:800]
                _article_cache[ref] = text
                return text

    except Exception as e:
        log.warning("fetch_article_text_from_db ref=%s: %s", ref, e)

    _article_cache[ref] = ""
    return ""


async def fetch_rulings_for_topic(topic_data: dict, pool) -> list[str]:
    """
    يبحث عن أحكام تمييز مرتبطة بالموضوع من DB مباشرة.
    """
    cache_key = "|".join(topic_data.get("search_patterns", [])[:3])
    if cache_key in _ruling_cache:
        return _ruling_cache[cache_key]

    rulings = []
    try:
        async with pool.acquire() as conn:
            for pattern in topic_data.get("search_patterns", [])[:3]:
                rows = await conn.fetch(
                    """SELECT content FROM chunks
                       WHERE is_active = true
                       AND (law_name ILIKE '%أحكام محكمة التمييز%' OR content ILIKE '%محكمة التمييز%' OR content ILIKE '%طعن رقم%')
                       AND content ILIKE $1
                       ORDER BY length(content) DESC
                       LIMIT 2""",
                    f"%{pattern}%",
                )
                for r in rows:
                    text = r["content"][:600]
                    if text not in rulings and len(r["content"]) > 100:
                        rulings.append(text)

                if len(rulings) >= 3:
                    break

    except Exception as e:
        log.warning("fetch_rulings_for_topic error: %s", e)

    result = rulings[:3]
    _ruling_cache[cache_key] = result
    return result


async def get_dynamic_knowledge_context(query: str, pool) -> str:
    """
    الدالة الرئيسية — تبحث في الخريطة عن الموضوع المناسب
    ثم تسحب النصوص الفعلية من DB وتبني سياق مهيكل.
    تحل محل get_knowledge_base_context (الصلبة).
    """
    if not pool:
        return ""

    query_lower = query.lower()
    matched_topics = []

    for topic_name, topic_data in TOPIC_TO_ARTICLES.items():
        for keyword in topic_data["keywords"]:
            if keyword in query_lower:
                matched_topics.append((topic_name, topic_data))
                break

    if not matched_topics:
        return ""

    context = "\n═══ سند قانوني مؤكد (إلزامي الاستخدام في المذكرات) ═══\n\n"

    for topic_name, topic_data in matched_topics[:3]:
        context += f"📌 الموضوع: {topic_name}\n\n"

        # سحب نصوص المواد من DB
        articles_found = 0
        for ref in topic_data["refs"]:
            text = await fetch_article_text_from_db(ref, pool)
            if text:
                article_num, law_name, law_num, law_year = _parse_ref(ref)
                label = f"م.{article_num}"
                if law_name:
                    label += f" {law_name}"
                if law_num and law_year:
                    label += f" {law_num}/{law_year}"
                context += f"  📜 {label}: {text[:500]}\n\n"
                articles_found += 1

        if articles_found == 0:
            # fallback: استخدم search_patterns كدليل
            context += f"  (لم يُعثر على نص — ابحث عن: {', '.join(topic_data['search_patterns'][:3])})\n\n"

        # حقن المبادئ القضائية (بروبمت 31)
        topic_principles = topic_data.get("principles", [])
        if topic_principles:
            context += f"  ⚖️ مبادئ محكمة التمييز (استشهد بها):\n"
            for p in topic_principles[:2]:
                context += f"    • «{p[:300]}»\n"
            context += "\n"

        # سحب أحكام تمييز مرتبطة
        rulings = await fetch_rulings_for_topic(topic_data, pool)
        if rulings:
            context += f"  ⚖️ أحكام محكمة التمييز المرتبطة:\n"
            for i, ruling in enumerate(rulings, 1):
                context += f"    [{i}] {ruling[:400]}\n\n"

        context += "---\n\n"

    context += (
        "⚠️ تعليمات:\n"
        "- في المذكرات والعقود: استخدم المواد والأحكام أعلاه إلزامياً\n"
        "- في الإجابات العادية: اشرح بأسلوبك بدون أرقام إلا إذا طلب المستخدم\n"
        "- اربط المادة بالحكم: \"وفقاً للمادة ___ ... وقد قضت محكمة التمييز بأن...\"\n"
    )

    return context


