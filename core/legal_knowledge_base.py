def get_knowledge_base_context(query: str) -> str:
    """
    Fallback — يرجع مراجع بدون نصوص DB.
    يُستخدم فقط إذا لم يتوفر pool (اختبارات / ollama).
    """
    query_lower = query.lower()
    matched = []

    for topic_name, topic_data in TOPIC_TO_ARTICLES.items():
        for kw in topic_data["keywords"]:
            if kw in query_lower:
                matched.append((topic_name, topic_data))
                break

    if not matched:
        return ""

    ctx = "\n═══ سند قانوني مؤكد (إلزامي الاستخدام) ═══\n\n"
    for topic_name, topic_data in matched[:3]:
        ctx += f"📌 {topic_name}:\n"
        for ref in topic_data["refs"]:
            article_num, law_name, law_num, law_year = _parse_ref(ref)
            label = f"م.{article_num}"
            if law_name:
                label += f" {law_name}"
            if law_num and law_year:
                label += f" {law_num}/{law_year}"
            ctx += f"  📜 {label}\n"
        ctx += "\n"

    ctx += "⚠️ استخدم المواد أعلاه بالصيغة: 'وفقاً للمادة ___ من قانون ___ رقم ___ لسنة ___'\n"
    return ctx


