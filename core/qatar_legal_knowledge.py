        ("إداري",  ["إداري", "وزارات", "وزراء", "خدمة مدنية", "حكومي", "ديوان"]),
        ("دستوري", ["دستور", "دستوري"]),
        ("مالي",   ["مالي", "مصرف", "بنك", "ضريبة", "ضرائب", "مالية", "نقد"]),
        ("عقاري",  ["عقاري", "أراضي", "أرض", "عقار", "إيجار", "ملكية"]),
