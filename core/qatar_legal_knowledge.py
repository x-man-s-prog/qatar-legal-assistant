def match_known_answer(question: str) -> Optional[str]:
    """
    يتحقق إذا كان السؤال يُطابق إجابة معلَّبة.

    Returns:
        str  — الإجابة الكاملة إذا وُجدت
        None — إذا لم تُوجد مطابقة
    """
    if not question or not question.strip():
        return None
    q = question.strip()
    for key, pattern in _KNOWN_ANSWER_PATTERNS:
        if pattern.search(q):
            return QATAR_KNOWN_ANSWERS.get(key)
    return None


def is_non_crime(question: str) -> Optional[dict]:
    """
    يتحقق إذا كان السؤال يتعلق بفعل غير مجرَّم في قطر.

    Returns:
        dict — معلومات الفعل غير المجرَّم {"الحكم": ..., "التفسير": ..., "ملاحظة": ...}
        None — إذا لم يكن الفعل في قائمة غير المجرَّمات
    """
    if not question or not question.strip():
        return None
    q = question.strip()
    for name, pattern in _NON_CRIME_PATTERNS:
        if pattern.search(q):
            return Qatar_NON_CRIMES.get(name)
    return None


def get_domain_threshold(domain: str) -> float:
    """يُعيد عتبة الثقة المناسبة للمجال القانوني."""
    if not domain:
        return DOMAIN_THRESHOLDS["default"]
    d = domain.lower().strip()
    # مطابقة مباشرة
    if d in DOMAIN_THRESHOLDS:
        return DOMAIN_THRESHOLDS[d]
    # مطابقة جزئية
    for k, v in DOMAIN_THRESHOLDS.items():
        if k in d or d in k:
            return v
    return DOMAIN_THRESHOLDS["default"]


