# ── أنماط regex للتطبيع التلقائي ─────────────────────────────────────────────
# كل عنصر: (نمط، canonical)
_LAW_NORMALIZE_PATTERNS: list[tuple] = [
    # "قانون رقم (X) لسنة YYYY بإصدار <اسم>"  →  "<اسم> رقم X/YYYY"
    (
        re.compile(
            r"قانون\s+رقم\s+[(\[]?(\d+)[)\]]?\s*لسنة\s*،?\s*(\d{4})\s*بإصدار\s+(.+?)$",
            re.UNICODE | re.DOTALL,
        ),
        lambda m: f"{m.group(3).strip()} رقم {m.group(1)}/{m.group(2)}",
    ),
    # "قانون X الصادر بالقانون رقم (N)لسنة ،YYYY..."
    (
        re.compile(
            r"(.+?)\s+الصادر\s+بالقانون\s+رقم\s+[(\[]?(\d+)[)\]]?\s*لسنة\s*،?\s*(\d{4})",
            re.UNICODE,
        ),
        lambda m: f"{m.group(1).strip()} رقم {m.group(2)}/{m.group(3)}",
    ),
    # "مرسوم بقانون رقم (N)لسنة YYYYبإصدار <اسم>"
    (
        re.compile(
            r"مرسوم\s+بقانون\s+رقم\s+[(\[]?(\d+)[)\]]?\s*لسنة\s*(\d{4})\s*بإصدار\s+(.+?)$",
            re.UNICODE | re.DOTALL,
        ),
        lambda m: f"{m.group(3).strip()} رقم {m.group(1)}/{m.group(2)}",
    ),
    # "قانون رقم (N)لسنة YYYYبشأن <موضوع>"
    (
        re.compile(
            r"قانون\s+رقم\s+[(\[]?(\d+)[)\]]?\s*لسنة\s*(\d{4})\s*بشأن\s+(.+?)$",
            re.UNICODE | re.DOTALL,
        ),
        lambda m: f"قانون {m.group(3).strip()} رقم {m.group(1)}/{m.group(2)}",
    ),
]


def _clean_law_name(name: str) -> str:
    """تنظيف أساسي لاسم القانون (مسافات، فواصل زائدة فقط — بلا تغيير للأقواس)."""
    if not name:
        return name
    # إزالة whitespace زائد
    name = " ".join(name.split())
    # إزالة الفاصلة قبل السنة: "لسنة ،2004" → "لسنة 2004"
    name = re.sub(r"لسنة\s*،\s*(\d)", r"لسنة \1", name)
    # توحيد مسافة "رقم(" : "رقم(16)" → "رقم (16)"
    name = re.sub(r"(رقم)\s*\(", r"\1 (", name)
    # توحيد "DIGIT)عربي" : "(16)لسنة" → "(16) لسنة"
    name = re.sub(r"\)([ا-ي])", r") \1", name)
    # إزالة مسافات مضاعفة
    name = " ".join(name.split())
    return name.strip()


# قاموس مُنظَّف مُبنى عند تحميل الوحدة — مفاتيح مُنظَّفة أيضاً
_LAW_ALIASES_NORMALIZED: dict[str, str] = {}


def _build_normalized_aliases() -> None:
    """يبني _LAW_ALIASES_NORMALIZED من LAW_ALIASES بتطبيق _clean_law_name على المفاتيح."""
    global _LAW_ALIASES_NORMALIZED
    _LAW_ALIASES_NORMALIZED = {}
    for alias, canonical in LAW_ALIASES.items():
        cleaned_key = _clean_law_name(alias)
        _LAW_ALIASES_NORMALIZED[cleaned_key] = canonical
        # أضف أيضاً الاختصار (أول 70 محرف)
        short_key = cleaned_key[:70].rstrip()
        if short_key and short_key not in _LAW_ALIASES_NORMALIZED:
            _LAW_ALIASES_NORMALIZED[short_key] = canonical


_build_normalized_aliases()


def normalize_law_name(name: str) -> str:
    """
    يُطبِّع اسم القانون إلى صيغته الكنونية الموحَّدة.

    الخطوات:
    1. تنظيف النص
    2. بحث مباشر في _LAW_ALIASES_NORMALIZED (مُنظَّف)
    3. مطابقة جزئية (prefix matching)
    4. تطبيق أنماط regex
    5. إرجاع النص المُنظَّف إذا لم يتطابق شيء

    مثال:
        >>> normalize_law_name("قانون رقم (11) لسنة 2004 بإصدار قانون العقوبات")
        'قانون العقوبات رقم 11/2004'
    """
    if not name or not isinstance(name, str):
        return name or ""

    cleaned = _clean_law_name(name)

    # 1. مطابقة مباشرة في الأسماء المُنظَّفة
    if cleaned in _LAW_ALIASES_NORMALIZED:
        return _LAW_ALIASES_NORMALIZED[cleaned]

    # 2. مطابقة جزئية — prefix من 80 محرف
    prefix80 = cleaned[:80]
    for alias_clean, canonical in _LAW_ALIASES_NORMALIZED.items():
        if alias_clean and (
            alias_clean.startswith(prefix80)
            or prefix80.startswith(alias_clean[:65])
        ):
            return canonical

    # 3. أنماط regex
    for pattern, formatter in _LAW_NORMALIZE_PATTERNS:
        m = pattern.search(cleaned)
        if m:
            try:
                result = formatter(m)
                # تنظيف: إزالة "والقوانين المعدلة له" وما بعدها
                result = re.sub(
                    r"\s*(والقوانين المعدلة له|والقوانين المعدلة"
                    r"|,\s*والقوانين.*|وتعديلاته).*$",
                    "",
                    result,
                ).strip()
                # تنظيف: إزالة نهايات مقطوعة للاسم
                result = re.sub(r"\s+له,$", "", result).strip()
                return result
            except Exception:
                pass

    # 4. إرجاع المُنظَّف
    return cleaned


