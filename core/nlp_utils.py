def _law_year_score_boost(law_year: str) -> float:
    """
    منحة درجة خفيفة للقوانين الحديثة — تُرجّح الأحدث عند تساوي الصلة.
    مُعايَرة بعناية: لا تُفسد ترتيب نتائج الصلة، فقط تُرجّح عند التعادل.
    """
    try:
        y = int(str(law_year).strip()) if law_year else 0
        if y <= 0:              return 0.0
        if y >= 2022:           return 0.03   # أحدث → أولوية خفيفة
        if y >= 2015:           return 0.02
        if y >= 2005:           return 0.01
        if y >= 2000:           return 0.00   # محايد
        if y >= 1990:           return -0.03  # قديم نسبياً
        if y >= 1980:           return -0.06  # قديم
        if y >= 1970:           return -0.10  # قديم جداً
        return -0.14                           # قديم جداً جداً — يُعرض فقط إذا لم يُوجد بديل
    except (ValueError, TypeError):
        return 0.0


def _deduplicate_law_versions(chunks: list[dict]) -> list[dict]:
    """
    يُزيل النسخ القديمة من نفس القانون — يحتفظ بالأحدث فقط.
    ⚠️ القاعدة الأساسية:
      • نفس القانون + سنة أحدث  → احذف القديم واحتفظ بالجديد (جميع مواده)
      • نفس القانون + سنة أقدم  → تجاهل (القديم مستبعَد)
      • نفس القانون + نفس السنة + مادة مختلفة → احتفظ بهما معاً ✓
      • نفس القانون + نفس السنة + نفس المادة → احتفظ بالأعلى درجةً
    مثال: قانون العقوبات 2004 مواد 335،336،338،339 → تبقى الأربعة جميعها.
    مثال: قانون الإجراءات الجنائية 1971 + 2004 → يبقى 2004 فقط.
    """
    _strip_re = re.compile(
        r'(الصادر\s+بالقانون|المعدل\s+بالقانون|رقم\s*\(?\d+\)?\s*لسنة\s*\(?\d+\)?'
        r'|قانون\s+رقم|مرسوم\s+رقم|أمر\s+أميري\s+رقم|\d{4}|\(\s*\d+\s*\)|\d+)'
    )

    def _subject_key(name: str) -> str:
        """
        مفتاح موضوع القانون — بدون أرقام وسنوات وكلمات إصدار.
        يُعيد نصاً قصيراً يمثّل هوية القانون بغض النظر عن رقمه وسنته.
        مثال: "قانون الإجراءات الجنائية رقم 23 لسنة 2004"
               "قانون الإجراءات الجنائية رقم 1 لسنة 1971"
        → كلاهما → "قانون الاجراءات الجنائيه"
        """
        n = _strip_re.sub('', name)
        n = normalize_ar(n)
        # احذف كلمات شائعة لا تميّز الموضوع
        stop_words = [
            'بشان', 'بشأن', 'بإصدار', 'باصدار', 'تعديل', 'بعض', 'أحكام', 'احكام',
            'الصادر', 'والقوانين', 'المعدله', 'المعدل', 'وتعديلاته', 'وتعديلاتها',
            'المنظم', 'المنظمه', 'المتعلق', 'المتعلقه', 'في', 'شأن', 'مجال'
        ]
        for stop in stop_words:
            n = n.replace(normalize_ar(stop), ' ')
        n = re.sub(r'\s+', ' ', n).strip()
        # خذ أول 40 حرف فقط لتمثيل الموضوع
        return n[:40]

    # subject_key → أفضل سنة رُصدت لهذا القانون
    seen_law_version: dict = {}   # subject_key → best_year
    # (subject_key, article_number) → موضع المادة في result
    seen_article: dict = {}
    result: list = []

    for ch in chunks:
        subj = _subject_key(ch.get('law_name', ''))
        if not subj or len(subj) < 5:
            result.append(ch)
            continue
        try:
            y = int(str(ch.get('law_year', '') or '0').strip())
        except ValueError:
            y = 0
        art = str(ch.get('article_number', '') or '').strip()

        if subj in seen_law_version:
            best_y = seen_law_version[subj]
            if y > best_y:
                # نسخة أحدث — احذف كل نتائج النسخة القديمة
                result = [r for r in result
                          if _subject_key(r.get('law_name', '')) != subj]
                # أعد بناء seen_article بعد الحذف
                seen_article = {
                    k: new_idx
                    for new_idx, r in enumerate(result)
                    for k in [(_subject_key(r.get('law_name', '')),
                                str(r.get('article_number', '') or '').strip())]
                    if k[0] != subj
                }
                seen_law_version[subj] = y
                art_key = (subj, art)
                if art_key not in seen_article:
                    seen_article[art_key] = len(result)
                    result.append(ch)
                continue
            elif y < best_y:
                continue  # نسخة أقدم — تجاهل

        # نفس السنة أو أول مرة نرى هذا القانون
        art_key = (subj, art)
        if art_key in seen_article:
            # نفس المادة — احتفظ بالأعلى درجةً
            old_idx = seen_article[art_key]
            if float(ch.get('score', 0)) > float(result[old_idx].get('score', 0)):
                result[old_idx] = ch
        else:
            # مادة جديدة من نفس القانون — أضفها
            seen_article[art_key] = len(result)
            seen_law_version[subj] = max(y, seen_law_version.get(subj, 0))
            result.append(ch)

    return result


def detect_legal_domain(keywords: list[str]) -> str | None:
    """يكتشف مجال القانون من الكلمات المفتاحية للسؤال"""
    _pfx = re.compile(r'^(وال|فال|بال|كال|ال|و|ف|ب|ك)')
    kw_set = set()
    for kw in keywords:
        n = normalize_ar(kw)
        kw_set.add(n)
        kw_set.add(_pfx.sub('', n))
    best, best_score = None, 0
    for domain, info in _LEGAL_DOMAINS.items():
        score = len(kw_set & info['kws'])
        if score > best_score:
            best_score, best = score, domain
    return best if best_score > 0 else None

def expand_keywords_with_synonyms(kws: list[str]) -> list[str]:
    """
    يضيف مرادفات قانونية للبحث الموسّع.
    مثال: ['ترخيص'] → ['ترخيص', 'رخصه', 'تصريح']
    """
    _pfx = re.compile(r'^(وال|فال|بال|كال|ال)')
    extra, seen = [], {normalize_ar(k) for k in kws}
    for kw in kws:
        n = normalize_ar(kw)
        n_clean = _pfx.sub('', n)
        for key in (n, n_clean):
            for s in _LEGAL_SYNONYMS.get(key, [])[:3]:   # أقصاه 3 لتوسيع الاسترجاع مع تقليل الضوضاء
                sn = normalize_ar(s)
                if sn not in seen:
                    seen.add(sn); extra.append(s)
    return list(dict.fromkeys(kws + extra))[:16]

