# إدارة السياق — يستخدم ctx_manager المتقدم أو الاحتياطي
# ══════════════════════════════════════════════════════════
_SESSION_SUMMARIES: dict[str, dict] = {}   # fallback: sid → {summary, legal_facts, cited_laws}

async def _summarize_session(sid: str, history: list[dict]) -> dict:
    """
    يُلخّص المحادثة الطويلة.
    عند توفر ctx_manager → يُفوَّض له.
    بدونه → يعمل بالتلخيص الأساسي.
    """
    # إذا ctx_manager متاح → يتولى الأمر بنفسه في maybe_summarize
    if app_state.CTX_AVAILABLE:
        return {}

    if len(history) < 6:
        return {}
    conv_text = "\n".join(
        f"{'مستخدم' if m['role'] == 'user' else 'مساعد'}: {m['content'][:300]}"
        for m in history[-10:]
    )
    msgs = [{"role": "user", "content": f"المحادثة:\n{conv_text}"}]
    try:
        if app_state.GW_AVAILABLE:
            raw = await app_state.llm_gw.call(system=SUMMARIZE_SYSTEM, messages=msgs, max_tokens=400)
        elif GEMINI_KEY:
            parts = []
            async for t in stream_gemini(SUMMARIZE_SYSTEM, msgs, max_tokens=400):
                parts.append(t)
            raw = "".join(parts)
        elif ANTHROPIC_KEY:
            raw = await call_claude(SUMMARIZE_SYSTEM, msgs, MODEL_CLAUDE_FAST, max_tokens=400)
        else:
            return {}
        m = re.search(r'\{.*\}', raw, re.DOTALL)
        if m:
            result = json.loads(m.group())
            _SESSION_SUMMARIES[sid] = result
            log.info("session summary (fallback): sid=%s facts=%d", sid, len(result.get("legal_facts",[])))
            return result
    except Exception as e:
        log.debug("_summarize_session: %s", e)
    return {}

def _build_context_prefix(sid: str, history: list[dict]) -> str:
    """
    يبني بادئة السياق.
    عند توفر ctx_manager → يستخدم build_prefix() المتقدم (ملخص + حقائق + قوانين).
    بدونه → يستخدم الملخص الأساسي.
    """
    # ── ctx_manager المتقدم (الأفضل) ──
    if app_state.CTX_AVAILABLE:
        return app_state.ctx_manager.build_prefix(sid)

    # ── fallback: الملخص الأساسي ──
    summary = _SESSION_SUMMARIES.get(sid, {})
    if not summary:
        return ""
    parts = []
    if summary.get("summary"):
        parts.append(f"[ملخص المحادثة السابقة: {summary['summary'][:200]}]")
    if summary.get("legal_facts"):
        facts = " | ".join(summary["legal_facts"][:3])
        parts.append(f"[الحقائق القانونية المتفق عليها: {facts}]")
    if summary.get("cited_laws"):
        laws = " | ".join(summary["cited_laws"][:3])
        parts.append(f"[القوانين المستشهد بها: {laws}]")
    return "\n".join(parts) + "\n\n" if parts else ""


async def _run_background_summarize(sid: str, history: list[dict]):
    """
    مهمة خلفية للتلخيص — تستخدم ctx_manager إذا كان متاحاً وإلا الاحتياطي.
    """
    if app_state.CTX_AVAILABLE:
        # بناء دالة LLM مناسبة للتلخيص
        async def _llm_for_summary(system_prompt: str, messages: list) -> str:
            if app_state.GW_AVAILABLE:
                return await app_state.llm_gw.call(system=system_prompt, messages=messages, max_tokens=400)
            if ANTHROPIC_KEY:
                return await call_claude(system_prompt, messages, MODEL_CLAUDE_FAST, max_tokens=400)
            elif GEMINI_KEY:
                parts = []
                async for t in stream_gemini(system_prompt, messages, max_tokens=400):
                    parts.append(t)
                return "".join(parts)
            return ""   # Ollama: لا تلخيص (بطيء)
        await app_state.ctx_manager.maybe_summarize(sid, _llm_for_summary)
    else:
        await _summarize_session(sid, history)

