def _detect_fixed_phrases(q: str) -> list[str]:
    """
    يكتشف العبارات القانونية المركبة في السؤال ويعيد قائمة بنسخ البحث.
    مثال: 'رد الاعتبار' → ['رد الاعتبار', 'رد اعتبار', 'إعادة الاعتبار']
    """
    q_norm = normalize_ar(q)
    # احذف ال من النص المعيّر للمطابقة الأوسع
    q_stripped = re.sub(r'ال', '', q_norm)
    found: list[str] = []
    for ph_norm, variants in _FIXED_PHRASE_NORMS.items():
        ph_stripped = re.sub(r'ال', '', ph_norm)
        # مطابقة بـ ال أو بدونها
        if ph_norm in q_norm or (len(ph_stripped) >= 5 and ph_stripped in q_stripped):
            for v in variants[:2]:
                if v not in found:
                    found.append(v)
    return found[:6]

def _al_phrase_variants(phrase: str) -> list[str]:
    """
    يولّد متغيرات الجملة مع/بدون ال للكلمة الثانية.
    مثال: 'رد اعتبار' → ['رد اعتبار', 'رد الاعتبار']
    مثال: 'رد الاعتبار' → ['رد الاعتبار', 'رد اعتبار']
    """
    words = phrase.split()
    variants = [phrase]
    if len(words) == 2:
        w1, w2 = words
        if not w2.startswith('ال') and len(w2) >= 3:
            variants.append(f"{w1} ال{w2}")
        elif w2.startswith('ال') and len(w2) > 4:
            variants.append(f"{w1} {w2[2:]}")
    elif len(words) == 3:
        # عبارة ثلاثية: جرّب إضافة/حذف ال من الكلمة الأخيرة
        w1, w2, w3 = words
        if not w3.startswith('ال') and len(w3) >= 3:
            variants.append(f"{w1} {w2} ال{w3}")
        elif w3.startswith('ال') and len(w3) > 4:
            variants.append(f"{w1} {w2} {w3[2:]}")
    return variants

def kw_variants(w: str) -> list[str]:
    """
    يولّد متغيرات الكلمة للبحث المتسامح مع أخطاء استخراج PDF:
    - الكلمة الأصلية
    - بعد تطبيع الألف (كل أشكاله → ا) للتعامل مع بدائل الألف
    - بعد حذف البادئة (ال، ل، ب…) من الأصلية والمعيارية
    - جذر أقصر يحذف حرف الألف الأول (للكلمات كالأقصى → قصى)
    """
    n = normalize_ar(w)
    v: dict = {}
    v[w] = None   # الكلمة الأصلية (للـ ILIKE الدقيق)
    v[n] = None   # المعيارية
    # حاول حذف البادئة من الأصلية والمعيارية
    for src in [w, n]:
        for p in _PFXS:
            if src.startswith(p) and len(src) - len(p) >= 3:
                stripped = src[len(p):]
                v[stripped] = None
                v[normalize_ar(stripped)] = None
                # جذر أعمق: احذف ألف البداية إذا كان الجذر يبدأ بألف
                # (يعالج: الأقصى → أقصى → قصى — لأن PDF يخزّن الألف مشوهة)
                stem2 = normalize_ar(stripped)
                if stem2 and stem2[0] in 'اأإآٱ' and len(stem2) - 1 >= 3:
                    v[stem2[1:]] = None  # احذف الألف الأولى → قصى
                break
    # أضف توسعات الأفعال القانونية (عقوبة→يعاقب، سرقة→سرق، طلاق→طلّق، رد→رد الاعتبار...)
    n_clean = re.sub(r'^(وال|فال|بال|كال|ال)', '', n)
    for key in (n, n_clean):
        for exp in _LEGAL_EXPANSIONS.get(key, []):
            v[exp] = None
            v[normalize_ar(exp)] = None
    # السماح بالمصطلحات القانونية القصيرة (حرفان)
    return [x for x in v if len(x) >= 3 or normalize_ar(x) in _SHORT_LEGAL_TERMS]

def extract_kw(q: str) -> list[str]:
    """يستخرج الكلمات المفتاحية الأصلية من السؤال (بدون تعيير مسبق)"""
    words = _AR_LETTERS.findall(q)  # حروف عربية فقط — لا ؟ ، ؛
    seen_norm: dict = {}
    result: list = []
    for w in words:
        n = normalize_ar(w)
        # جرّد بادئة و (حرف العطف) إذا أضيفت للكلمة — وحضانة → حضانة
        if n.startswith('و') and len(n) >= 5:
            n_stripped = n[1:]
            if n_stripped not in _STOP:
                n = n_stripped
                w = w[1:] if w.startswith('و') else w  # أزل 'و' من الأصلية أيضاً
        if n not in _STOP and n not in seen_norm and (len(n) >= 3 or n in _SHORT_LEGAL_TERMS):
            seen_norm[n] = None
            result.append(w)   # الأصلية للـ ILIKE الدقيق
    return result[:10]

def extract_phrases(q: str) -> list[str]:
    """
    يستخرج عبارات ثنائية (bigrams) وثلاثية (trigrams) من السؤال.
    تُستخدم للبحث عن تطابق عبارة متتالية في المحتوى.
    مثال: 'ساعات العمل' ، 'الحد الأقصى لساعات'
    """
    words = _AR_LETTERS.findall(q)  # جميع الكلمات بالترتيب
    phrases: list = []
    def _word_ok(w: str) -> bool:
        """يقبل الكلمة في العبارة: 3 أحرف فأكثر، أو 2 أحرف إذا كانت مصطلحاً قانونياً قصيراً"""
        n = normalize_ar(w)
        return (len(n) >= 3 or n in _SHORT_LEGAL_TERMS) and n not in _STOP

    # عبارات ثنائية: كلمتان متتاليتان (الأولى أو الثانية ليست أداة)
    for i in range(len(words) - 1):
        w1, w2 = words[i], words[i+1]
        if _word_ok(w1) and _word_ok(w2):
            phrase = f"{w1} {w2}"
            if phrase not in phrases:
                phrases.append(phrase)
                # أضف متغير ال مباشرةً (رد اعتبار ↔ رد الاعتبار)
                for alt in _al_phrase_variants(phrase)[1:]:
                    if alt not in phrases:
                        phrases.append(alt)
    # عبارات ثلاثية: ثلاث كلمات متتالية
    for i in range(len(words) - 2):
        w1, w2, w3 = words[i], words[i+1], words[i+2]
        if _word_ok(w1) and _word_ok(w2) and _word_ok(w3):
            phrase = f"{w1} {w2} {w3}"
            if phrase not in phrases:
                phrases.append(phrase)
    return phrases[:8]

