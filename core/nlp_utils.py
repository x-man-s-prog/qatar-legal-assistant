def classify_query(q: str, history: list[dict]) -> str:
    """
    يصنّف الاستعلام:
    'greeting'      — تحية بسيطة
    'chat'          — محادثة اجتماعية عامة (من أنت؟ كيف حالك؟ إلخ)
    'love'          — مشاعر إيجابية وإطراء (أحبك، روعة، الأفضل)
    'confirm_agree' — تأكيد وموافقة (نعم، أكيد، متأكد، ماشي)
    'followup'      — متابعة/اعتراض على إجابة سابقة
    'off_topic'     — خارج النطاق تماماً (طبخ، رياضة...)
    'legal'         — استعلام قانوني (الافتراضي)
    """
    q_stripped = q.strip()
    q_norm = normalize_ar(q_stripped)
    word_count = len(q_stripped.split())

    # ── 0. إطراء/حب (أولوية عالية — قبل كل شيء) ──
    for pat in _LOVE_PATTERNS:
        if pat.search(q_stripped):
            return 'love'

    # ── 0b. تأكيد/موافقة (≤ 4 كلمات — أولوية عالية) ──
    if word_count <= 4:
        for pat in _CONFIRM_PATTERNS:
            if pat.search(q_stripped):
                return 'confirm_agree'

    # ── 1. تحية بسيطة (≤ 4 كلمات) ──
    if word_count <= 4:
        if q_norm in {normalize_ar(w) for w in _GREETING_WORDS} or q_stripped.lower() in _GREETING_WORDS:
            return 'greeting'
        for pat in _GREETING_PATTERNS:
            if pat.search(q_stripped):
                return 'greeting'

    # ── 2. محادثة اجتماعية (chat) ──
    for pat in _CHAT_PATTERNS:
        if pat.search(q_stripped):
            return 'chat'

    # ── 3. متابعة/اعتراض (فقط مع تاريخ) ──
    if len(history) >= 2:
        for pat in _FOLLOWUP_PATTERNS:
            if pat.search(q_stripped):
                return 'followup'

    # ── 4. خارج النطاق ──
    for pat in _OFF_TOPIC_PATTERNS:
        if pat.search(q_stripped):
            return 'off_topic'

    return 'legal'


_PREFIX_STRIP = re.compile(r'^(ال|وال|وي|يت|ست|مت|لل|بال|فال|كال)')

def _keyword_root(w: str) -> str:
    """يستخرج الجذر الأساسي للكلمة بعد حذف البوادئ الشائعة"""
    n = normalize_ar(w)
    return _PREFIX_STRIP.sub('', n) if len(n) > 4 else n

def _check_retrieval_relevance(q: str, chunks: list[dict],
                               extra_terms: list[str] | None = None) -> bool:
    """
    يتحقق من أن النتائج المسترجعة ذات صلة فعلية بالسؤال.
    خوارزمية التغطية: يحسب نسبة كلمات السؤال الموجودة في أفضل 5 نتائج.
    - إذا كان هناك phrase_match → نعم دائماً (تطابق عبارة = صلة مؤكدة)
    - إذا كانت التغطية ≥ 20% → نعم (أصلي أو موسَّع)
    - إذا كانت أقل → لا

    extra_terms: مصطلحات قانونية من query_engine — تُضاف لجذور الفحص.
    مثال: ["فصل تعسفي","إنهاء عقد العمل"] لـ "طردوني من الشغل"
    """
    if not chunks:
        return False

    # phrase_match = تطابق عبارة كاملة → ثقة عالية
    if any(ch.get('phrase_match') for ch in chunks[:3]):
        return True

    raw_kws = extract_kw(q)
    if not raw_kws or len(raw_kws) < 2:
        return True  # لا يمكن التحقق بكلمة واحدة

    # استخرج الجذور + المرادفات للبحث الشامل (يشمل المصطلحات القصيرة)
    q_roots = {_keyword_root(kw) for kw in raw_kws
               if len(kw) >= 3 or normalize_ar(kw) in _SHORT_LEGAL_TERMS}
    # أضف جذور من المرادفات للمطابقة الموسّعة
    expanded = expand_keywords_with_synonyms(raw_kws)
    for kw in expanded:
        r = _keyword_root(kw)
        if len(r) >= 3:
            q_roots.add(r)

    # ── مسار extra_terms من query_engine (العامية الموسَّعة) ──
    # يتطلب الآن تطابق مصطلحَين مختلفَين على الأقل لتجنب القبول العشوائي
    if extra_terms:
        et_roots = set()
        for term in extra_terms:
            for word in term.split():
                r = _keyword_root(word)
                if len(r) >= 3:
                    et_roots.add(r)
        # عدّ المصطلحات الموجودة في أفضل 8 chunks
        matched_roots: set[str] = set()
        for ch in chunks[:8]:
            content_text = normalize_ar(ch['content'][:600])
            for root in et_roots:
                if len(root) >= 4 and root in content_text:
                    matched_roots.add(root)
        # عتبة القبول — مصطلح واحد طويل (6+ حرف) يكفي كدليل على الصلة القانونية الدقيقة
        # مثال: "حضانه" (6 حرف) = مصطلح قانوني محدد يكفي وحده
        _has_long_domain_term = any(len(r) >= 6 for r in et_roots)
        _min_roots = 1 if _has_long_domain_term else 2
        if len(matched_roots) >= _min_roots:
            log.info("relevance check: fast-pass via extra_terms (%d/%d roots, min=%d)",
                     len(matched_roots), len(et_roots), _min_roots)
            return True

    # أضف جذور extra_terms للفحص العام أيضاً
    if extra_terms:
        for term in extra_terms:
            for word in term.split():
                r = _keyword_root(word)
                if len(r) >= 3:
                    q_roots.add(r)

    if not q_roots:
        return True

    # احسب التغطية: كم جذراً من جذور السؤال موجود في أفضل 8 نتائج (موسَّع من 5)
    covered_roots = set()
    for ch in chunks[:8]:
        content_text = normalize_ar(ch['content'][:800])
        for root in q_roots:
            # السماح بجذور 2 حرف إذا كانت مصطلحاً قانونياً قصيراً
            if (len(root) >= 3 or root in _SHORT_LEGAL_TERMS) and root in content_text:
                covered_roots.add(root)

    coverage = len(covered_roots) / max(len(q_roots), 1)
    log.info("relevance check: coverage=%.0f%% (%d/%d roots) extra_terms=%d",
             coverage * 100, len(covered_roots), len(q_roots), len(extra_terms or []))

    # 25% تغطية مطلوبة — ترفع دقة الصلة وتمنع الاستشهاد بقوانين غير ذات صلة
    return coverage >= 0.25


def _has_clear_legal_domain(q: str, cot_domain: str = "") -> bool:
    """
    يتحقق إذا كان الاستعلام يخص مجالاً قانونياً واضحاً.
    يُستخدم لتخفيف صرامة _check_retrieval_relevance عندما يكون المجال محدداً.
    يستخدم:
      (أ) نتيجة COT إذا كانت محددة
      (ب) classify_query_domain من domain_relevance_engine (مباشر، بدون LLM)
    """
    # (أ) COT domain
    if cot_domain and cot_domain not in ("", "إجرائي", "غير محدد", "procedural"):
        return True
    # (ب) DRE classify — سريع، يعتمد على كلمات مفتاحية فقط
    _dre_fn = getattr(app_state, "dre_classify", None)
    if app_state.DRE_AVAILABLE and _dre_fn:
        try:
            domain, conf = _dre_fn(q)
            if domain and conf >= 0.15:  # عتبة منخفضة — حتى إشارة بسيطة كافية
                log.info("_has_clear_legal_domain: DRE→%s (conf=%.2f)", domain, conf)
                return True
        except Exception:
            pass
    return False


