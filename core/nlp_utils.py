# كشف الغموض — Ambiguity Detection
# ══════════════════════════════════════════════════════════
_AMBIGUOUS_PATTERNS = [
    re.compile(p, re.IGNORECASE | re.UNICODE) for p in [
        r'^(ما|ماذا|كيف|هل)\s+(حكم|عقوبة|جزاء)\s+(هذا|هذه|ذلك|ذلك)\s*[؟?]?$',
        r'^(هل\s+هذا\s+صحيح|هل\s+هذا\s+قانوني|هل\s+يجوز\s+هذا)\s*[؟?]?$',
        r'^(ما\s+رأيك|شو\s+رأيك|شو\s+تعتقد)\s*[؟?]?$',
        r'^(وضح\s+لي|اشرح\s+لي|قولي)\s*[؟?]?$',
        r'^[^؟?\u0600-\u06FF]{0,5}(هذا|هذه|ذلك|تلك)[^؟?\u0600-\u06FF]{0,5}[؟?]?$',
    ]
]

_AMBIGUOUS_SINGLE_WORDS = {
    normalize_ar(w) for w in [
        'قانوني', 'صحيح', 'جائز', 'مسموح', 'ممنوع', 'حكم',
        'عقوبة', 'جزاء', 'الحكم', 'الجزاء'
    ]
}

def _detect_ambiguity(q: str, kws: list[str]) -> tuple[bool, str]:
    """
    يكتشف إذا كان السؤال غامضاً جداً ويحتاج توضيحاً.
    يُعيد: (needs_clarification, clarification_question)

    حالات الغموض:
    1. السؤال قصير جداً (≤3 كلمات) مع كلمات إشارة (هذا/تلك) بدون سياق
    2. السؤال يستخدم ضمير بدون مرجع واضح في المحادثة
    3. السؤال يطلب حكماً بدون تحديد الموضوع
    """
    q_stripped = q.strip()
    word_count = len(q_stripped.split())

    # لا نُعيد طلب توضيح إذا كان السؤال طويلاً بما يكفي
    if word_count > 8:
        return False, ""

    # كشف الأنماط الغامضة
    for pat in _AMBIGUOUS_PATTERNS:
        if pat.search(q_stripped):
            return True, "سؤالك يحتاج توضيحاً قليلاً — هل يمكنك تحديد الموضوع أو الحالة التي تسأل عنها؟"

    # سؤال مكوّن من كلمة واحدة أو كلمتين فقط
    if word_count <= 2:
        q_norm = normalize_ar(q_stripped)
        if q_norm in _AMBIGUOUS_SINGLE_WORDS:
            return True, "هل يمكنك توضيح ما تقصده بالضبط؟ مثلاً: عقوبة ماذا؟ في أي موضوع؟"

    return False, ""


