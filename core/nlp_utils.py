def enrich_followup_query(query: str, history: list[dict]) -> str:
    """
    إذا كانت الرسالة قصيرة وتابعة فعلاً، يضيف سياق المحادثة السابقة للاستعلام.
    ⚠️ تم تشديد الشروط لمنع تلوّث الأسئلة المستقلة بسياق المحادثة السابقة.
    """
    if not history or len(query.split()) > 8:
        return query

    # استخرج آخر سؤال من المحادثة
    last_user_q = ""
    for msg in reversed(history):
        if msg.get("role") == "user" and len(msg.get("content", "")) > 10:
            last_user_q = msg["content"][:200]
            break

    if not last_user_q:
        return query

    word_count = len(query.split())

    # ⛔ قائمة موسّعة: إذا فيها كلمة مستقلة ← لا يُعتبر followup أبداً
    _INDEPENDENT_MARKERS = [
        # رواتب ودرجات
        'راتب', 'رواتب', 'مكافأة', 'مكافاة', 'درجة', 'علاوة', 'بدل', 'ترقية',
        # أحوال شخصية
        'طلاق', 'حضانة', 'نفقة', 'زواج', 'خلع', 'عدة', 'ميراث', 'وصية', 'نسب',
        # جنائي
        'سرقة', 'قتل', 'ضرب', 'شيك', 'عقوبة', 'جنحة', 'جناية', 'غرامة',
        'مخدرات', 'تعاطي', 'كيميائية', 'محظورة', 'حشيش', 'كبتاجون',
        'ابتزاز', 'تحرش', 'تشهير', 'قذف', 'تزوير', 'رشوة', 'اختلاس', 'نصب', 'احتيال',
        # عمل
        'فصل', 'استقالة', 'انقطاع', 'إنهاء', 'مكافأة نهاية',
        # مدني/تجاري
        'عقد', 'إيجار', 'شركة', 'تأسيس', 'ترخيص', 'تعويض', 'ضمان',
        # جداول ومواد
        'جدول', 'نص المادة', 'المادة رقم', 'مادة',
        # مرور
        'مرور', 'حادث', 'رخصة', 'مخالفة',
        # أوامر واستفسارات مستقلة
        'اختصر', 'عرفني', 'كم مبدأ', 'احسب', 'كم',
        'ما هو', 'ما هي', 'ما هو حكم', 'ما هي عقوبة', 'ما حكم',
        'مرحبا', 'مرحبا', 'السلام', 'أهلا',
    ]
    _has_own_topic = any(m in query for m in _INDEPENDENT_MARKERS)

    # إذا فيه موضوع مستقل واضح → ليس followup
    if _has_own_topic:
        return query

    # تحقق من أنماط المتابعة الصريحة فقط
    is_followup = False
    for pat in _FOLLOWUP_PATTERNS:
        if pat.search(query.strip()):
            is_followup = True
            break

    # أو إذا كانت قصيرة جداً (≤ 3 كلمات) بدون موضوع مستقل
    if not is_followup and word_count <= 3 and len(history) >= 2:
        is_followup = True

    if is_followup:
        enriched = f"بخصوص سؤالي السابق ({last_user_q[:100]}): {query}"
        return enriched

    return query

