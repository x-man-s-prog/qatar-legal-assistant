def get_history(sid: str) -> list[dict]:
    """يُعيد تاريخ المحادثة — يفضّل ctx_manager المتقدم إذا كان متاحاً"""
    if app_state.CTX_AVAILABLE:
        return app_state.ctx_manager.get_history(sid)
    # fallback: الذاكرة الأساسية
    now = time.time()
    expired = [k for k, t in app_state.session_ts.items() if now - t > SESSION_TTL]
    for k in expired:
        app_state.sessions.pop(k, None); app_state.session_ts.pop(k, None)
    app_state.session_ts[sid] = now
    return list(app_state.sessions[sid])

def add_to_history(sid: str, role: str, content: str):
    """يُضيف رسالة للتاريخ — يفضّل ctx_manager المتقدم إذا كان متاحاً"""
    if app_state.CTX_AVAILABLE:
        app_state.ctx_manager.add(sid, role, content)
        return
    # fallback: الذاكرة الأساسية
    app_state.sessions[sid].append({"role": role, "content": content[:1000]})
    app_state.session_ts[sid] = time.time()



