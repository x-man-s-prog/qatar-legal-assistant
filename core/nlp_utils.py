# إعادة الترتيب المحلية الدلالية — Semantic Local Re-ranking
# تعمل بدون LLM كطبقة أولى (سريعة جداً)
# ══════════════════════════════════════════════════════════
def _semantic_local_rerank(q: str, chunks: list[dict]) -> list[dict]:
    """
    يُعيد ترتيب المقاطع محلياً بناءً على:
    1. عدد كلمات السؤال الموجودة في المادة (تغطية مباشرة)
    2. وجود كلمات السؤال في اسم القانون (صلة عالية)
    3. قِصَر المادة (مادة مختصرة ومركّزة أفضل من طويلة مشتتة)
    4. درجة البحث الأصلية (score)
    """
    if not chunks or len(chunks) <= 3:
        return chunks

    q_norm = normalize_ar(q)
    q_roots = {_keyword_root(kw) for kw in extract_kw(q) if len(kw) >= 3}

    # أضف مرادفات للجذور
    expanded = expand_keywords_with_synonyms(list(extract_kw(q)))
    for kw in expanded:
        r = _keyword_root(kw)
        if len(r) >= 3:
            q_roots.add(r)

    if not q_roots:
        return chunks

    scored = []
    for ch in chunks:
        content_norm = normalize_ar(ch.get("content", "")[:1000])
        law_name_norm = normalize_ar(ch.get("law_name", ""))

        # (1) تغطية جذور السؤال في المحتوى
        content_coverage = sum(1 for r in q_roots if r in content_norm) / max(len(q_roots), 1)

        # (2) تغطية في اسم القانون (مضاعَفة — صلة أعلى)
        law_coverage = sum(1 for r in q_roots if len(r) >= 4 and r in law_name_norm) / max(len(q_roots), 1)

        # (3) مكافأة phrase_match
        phrase_bonus = 0.15 if ch.get("phrase_match") else 0.0

        # (4) مكافأة keyword_match
        kw_bonus = 0.05 if ch.get("keyword_match") else 0.0

        # (5) الدرجة الأصلية
        orig_score = float(ch.get("score", 0))

        # الدرجة المجمّعة
        # منطق: تغطية المحتوى أهم من الدرجة الأصلية — تمنع طغيان year_boost على الصلة الفعلية
        combined = (
            content_coverage * 0.45 +   # الأثقل — هل المحتوى يجيب فعلاً؟
            orig_score * 0.25 +          # الأصلية كمرجع (مُخففة لتجنب طغيان year_boost)
            law_coverage * 0.20 +        # اسم القانون يحمل الكلمات المفتاحية؟
            phrase_bonus +               # تطابق عبارة = +0.15
            kw_bonus                     # تطابق كلمي = +0.05
        )
        scored.append((combined, ch))

    scored.sort(key=lambda x: x[0], reverse=True)
    log.info("semantic_local_rerank: top=%s (%.3f)", scored[0][1].get("law_name","")[:30] if scored else "—", scored[0][0] if scored else 0)
    return [ch for _, ch in scored]


