def expand_gulf_dialect(query: str) -> str:
    """يحوّل اللهجة الخليجية للفصحى — العبارات المركّبة أولاً ثم المفردات."""
    result = query
    # أولاً: العبارات المركّبة (الأطول أولاً)
    for phrase in sorted(_GULF_PHRASES, key=len, reverse=True):
        if phrase in result:
            result = result.replace(phrase, _GULF_PHRASES[phrase])
    # ثانياً: الكلمات المفردة
    words = result.split()
    expanded = []
    for w in words:
        w_clean = w.strip('؟!.,،:')
        replacement = _GULF_DIALECT_MAP.get(w_clean)
        expanded.append(replacement if replacement else w)
    return ' '.join(expanded)

