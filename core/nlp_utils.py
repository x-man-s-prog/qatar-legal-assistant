# Chain of Verification — التحقق من الإجابة بعد التوليد
# ══════════════════════════════════════════════════════════
_LEGAL_CLAIM_PATTERNS = [
    # نمط: "يُعاقب بالحبس مدة X أو بغرامة X"
    re.compile(r'(يُعاقب|عقوبة|العقوبة|الجزاء|يُجازى).{0,60}(حبس|سجن|غرامة|إعدام|جلد)', re.UNICODE),
    # نمط: "وفقاً للمادة X من القانون Y"
    re.compile(r'(وفقاً?\s+للمادة|بموجب\s+المادة|نصّت\s+المادة|استناداً?\s+للمادة)\s*\(?\s*(\d+)', re.UNICODE),
    # نمط: "القانون رقم X لسنة Y"
    re.compile(r'القانون\s+رقم\s*\(?\s*(\d+)\s*\)?\s*لسنة\s*\(?\s*(\d+)', re.UNICODE),
]

def _chain_of_verification(answer: str, chunks: list[dict], q: str) -> tuple[str, list[str]]:
    """
    سلسلة التحقق من الإجابة:
    1. يستخرج الادعاءات القانونية من الإجابة
    2. يتحقق من كل ادعاء مقابل المقاطع المسترجعة
    3. يُضيف ملاحظة تحذيرية إذا وجد ادعاءات غير مدعومة

    يُعيد: (answer_مُحدَّثة, قائمة_التحذيرات)
    """
    warnings = []

    # (أ) فحص المواد القانونية المذكورة في الإجابة
    cited_articles = re.findall(
        r'المادة\s*\(?\s*(\d+)\s*\)?', answer
    )
    cited_laws = re.findall(
        r'القانون\s+رقم\s*\(?\s*(\d+)\s*\)?\s*لسنة\s*\(?\s*(\d+)', answer
    )

    # بناء مجموعة المواد الموجودة فعلاً في المقاطع
    valid_articles = {str(ch.get("article_number", "")).strip() for ch in chunks}
    valid_law_refs = {
        (str(ch.get("law_number", "")).strip(), str(ch.get("law_year", "")).strip())
        for ch in chunks
    }

    # تحقق من كل مادة مذكورة
    for art_num in cited_articles:
        if art_num not in valid_articles:
            # تحقق خفيف: هل هي في محتوى المقاطع على الأقل؟
            in_content = any(
                f"المادة ({art_num})" in ch.get("content", "") or
                f"المادة {art_num}" in ch.get("content", "")
                for ch in chunks
            )
            if not in_content:
                warnings.append(f"المادة ({art_num}) لم تُوجَد في النصوص المسترجعة")

    # تحقق من القوانين المذكورة
    for law_num, law_year in cited_laws:
        if (law_num, law_year) not in valid_law_refs:
            # تحقق خفيف في المحتوى
            in_content = any(
                (f"رقم ({law_num})" in ch.get("content", "") or
                 f"رقم {law_num}" in ch.get("content", ""))
                and (law_year in ch.get("content", "") or law_year == ch.get("law_year", ""))
                for ch in chunks
            )
            if not in_content:
                warnings.append(f"القانون رقم ({law_num}) لسنة {law_year} — لم يُتحقق منه في المصادر")

    # (ب) إضافة ملاحظة إذا كانت هناك تحذيرات
    if warnings:
        warning_text = (
            "\n\n---\n"
            "⚠️ **ملاحظة التحقق التلقائي:** بعض المراجع المذكورة أعلاه قد تحتاج "
            "مراجعة من مصدرها الأصلي (بوابة الميزان القانوني)."
        )
        if warning_text not in answer:
            answer += warning_text
        log.info("chain_of_verification: %d تحذير — %s", len(warnings), warnings[:2])

    return answer, warnings


