def _detect_topic(text: str) -> str:
    """يُحدد موضوع النص بناءً على الكلمات المفتاحية"""
    text_lower = text.strip()
    best_topic = ""
    best_count = 0
    for topic, keywords in _TOPIC_KEYWORDS.items():
        count = sum(1 for kw in keywords if kw in text_lower)
        if count > best_count:
            best_count = count
            best_topic = topic
    return best_topic


def _is_topic_change(query: str, history: list[dict]) -> bool:
    """يكشف إذا كان السؤال الحالي في موضوع مختلف عن المحادثة السابقة"""
    if not history:
        return True  # أول سؤال = موضوع جديد
    current_topic = _detect_topic(query)
    if not current_topic:
        return False  # لا نستطيع تحديد الموضوع → نفترض متابعة
    # ابحث عن آخر سؤال مستخدم
    for msg in reversed(history):
        if msg.get("role") == "user":
            prev_topic = _detect_topic(msg.get("content", ""))
            if prev_topic and prev_topic != current_topic:
                return True
            break
    return False


def filter_history_for_llm(query: str, history: list[dict], max_pairs: int = 2) -> list[dict]:
    """
    يُنقّي history قبل إرسالها للـ LLM لمنع تلوّث الإجابات.
    - إذا تغيّر الموضوع ← لا يُرسل history (يبدأ من صفر)
    - إذا نفس الموضوع ← يُرسل آخر max_pairs أزواج فقط
    """
    if not history:
        return []

    # كشف تغير الموضوع
    if _is_topic_change(query, history):
        log.info("topic_change_detected: q='%s' → history cleared for LLM", query[:60])
        return []

    # نفس الموضوع → أرسل آخر max_pairs أزواج (user+assistant) فقط
    # هذا يمنع تراكم السياق الطويل الذي يُربك النموذج
    max_msgs = max_pairs * 2
    if len(history) > max_msgs:
        return history[-max_msgs:]
    return list(history)


