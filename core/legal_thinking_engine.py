class OpposingArgumentEngine:
    """Generates realistic, bounded-language opposing-side arguments per issue."""

    def generate(self, query: str, issue_type: IssueType,
                  max_count: int = 4) -> list[str]:
        args_for_issue = _OPPOSING_ARGUMENTS.get(issue_type, [])
        if not args_for_issue:
            return []

        specific = []
        generic = []
        for marker, text in args_for_issue:
            if marker == "":
                generic.append(text)
            elif marker in query and text not in specific:
                specific.append(text)

        # Prefer specific matches; fill with generic up to max_count
        out = list(specific)
        for g in generic:
            if len(out) >= max_count:
                break
            if g not in out:
                out.append(g)
        return out[:max_count]


