# ══════════════════════════════════════════════════════════════
# LegalAnalysis Dataclass
# ══════════════════════════════════════════════════════════════

@dataclass
class LegalAnalysis:
    issue_type: IssueType = IssueType.UNKNOWN
    core_question: str = ""
    key_facts: list[str] = field(default_factory=list)
    supporting_facts: list[str] = field(default_factory=list)
    weakening_facts: list[str] = field(default_factory=list)
    opposing_arguments: list[str] = field(default_factory=list)
    proof_requirements: list[str] = field(default_factory=list)
    procedural_risk: str = ""
    authority_path: str = ""
    next_step: str = ""
    safe_summary: str = ""
    confidence: float = 0.0
    notes_internal: list[str] = field(default_factory=list)

    def is_substantive(self) -> bool:
        """Returns True if this analysis has meaningful content (not UNKNOWN + empty)."""
        if self.issue_type == IssueType.UNKNOWN:
            return False
        return bool(self.supporting_facts or self.weakening_facts
                    or self.opposing_arguments or self.proof_requirements
                    or self.next_step)


