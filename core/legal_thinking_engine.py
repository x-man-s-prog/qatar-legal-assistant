class FactQualityAnalyzer:
    """Extracts supporting/weakening facts + proof requirements per issue type."""

    def analyze(self, query: str, issue_type: IssueType) -> dict:
        patterns = _FACT_PATTERNS.get(issue_type, {})
        result = {"supporting": [], "weakening": [], "proof_needed": []}
        if not patterns:
            return result

        for marker, text in patterns.get("supporting", []):
            if marker in query and text not in result["supporting"]:
                result["supporting"].append(text)
        for marker, text in patterns.get("weakening", []):
            if marker in query and text not in result["weakening"]:
                result["weakening"].append(text)
        for marker, text in patterns.get("proof_needed", []):
            if marker in query and text not in result["proof_needed"]:
                result["proof_needed"].append(text)

        return result


