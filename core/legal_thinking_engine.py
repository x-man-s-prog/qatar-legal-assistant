# Integration API
# ══════════════════════════════════════════════════════════════

_engine: Optional[LegalThinkingEngine] = None


def get_legal_thinking_engine() -> LegalThinkingEngine:
    global _engine
    if _engine is None:
        _engine = LegalThinkingEngine()
    return _engine


def analyze_legal_issue(query: str) -> LegalAnalysis:
    """Convenience: full legal analysis pipeline."""
    return get_legal_thinking_engine().build_legal_analysis(query)


def enhance_with_legal_thinking(answer: str, query: str,
                                  force: bool = False) -> tuple[str, bool, Optional[LegalAnalysis]]:
    """
    If the query merits legal thinking, append structured analysis.
    Returns (enhanced_answer, applied_flag, analysis_or_None).
    Pure no-op for non-qualifying queries.
    """
    if not force and not should_activate_legal_thinking(query):
        return answer, False, None
    analysis = analyze_legal_issue(query)
    if not analysis.is_substantive():
        return answer, False, None
    section = format_legal_analysis(analysis)
    if not section:
        return answer, False, analysis
    base = (answer or "").rstrip()
    if base:
        enhanced = f"{base}\n\n---\n\n{section}"
    else:
        enhanced = section
    log.info("[LEGAL_BRAIN] enhanced: issue=%s conf=%.2f",
             analysis.issue_type.value, analysis.confidence)
    return enhanced, True, analysis
