# Triggers that indicate this is a consultation (not a factual query)
_CONSULTATION_TRIGGERS = [
    # Strength/weakness request
    "نقاط ضعفي", "نقاط قوتي", "موقفي", "وضعي",
    "ما موقفي", "كيف موقفي",
    # Yes/no with context
    "هل يكفي", "هل هذا يكفي", "هل أقدر", "هل يحق",
    # Opposing party
    "يحتج به", "يحتج", "يستخدمه ضدي", "ممكن يستخدمه",
    "الطرف الآخر", "الطرف الثاني", "صاحب العمل",
    # Evidence concerns
    "ما عندي", "محادثات واتساب", "ما أرسلت", "ما صار",
    "ما أعرف متى", "تأخرت",
    # Procedure questions
    "كيف تفصل", "كيف يتم", "ماذا أفعل", "الخطوة",
    # Multi-party
    "يحمّلوني", "يحمّلني", "المسؤولية",
]


class LegalThinkingEngine:
    """
    Main engine. Converts a user question into a structured LegalAnalysis.
    Runs AFTER query understanding, BEFORE final response generation.
    """

    def __init__(self):
        self._classifier = IssueClassifier()
        self._facts = FactQualityAnalyzer()
        self._opposing = OpposingArgumentEngine()
        self._authority = LegalAuthorityPathResolver()

    # ── Public API (spec-mandated methods) ──

    def identify_issue_type(self, query: str) -> tuple[IssueType, float]:
        return self._classifier.classify(query)

    def extract_key_facts(self, query: str) -> list[str]:
        """Extract salient factual markers directly stated in the query."""
        markers = []
        # Key personal-situation markers
        for m in ["تحويلات راتب", "محادثات واتساب", "رسائل", "شهود",
                   "اعتراف", "معترف", "صدر حكم", "ما أعرف متى", "تأخرت",
                   "قرار إداري", "ما عندي عقد", "ما أرسلت إنذار",
                   "حكم قضائي", "أدلة غير مباشرة", "شهود متناقضين",
                   "قرار جماعي", "منفصل عن زوجتي", "ما عندي سوابق"]:
            if m in query and m not in markers:
                markers.append(m)
        return markers[:8]

    def identify_supporting_facts(self, query: str,
                                    issue_type: IssueType) -> list[str]:
        return self._facts.analyze(query, issue_type).get("supporting", [])[:4]

    def identify_weakening_facts(self, query: str,
                                   issue_type: IssueType) -> list[str]:
        return self._facts.analyze(query, issue_type).get("weakening", [])[:4]

    def identify_opposing_arguments(self, query: str,
                                      issue_type: IssueType) -> list[str]:
        return self._opposing.generate(query, issue_type)

    def identify_proof_requirements(self, query: str,
                                      issue_type: IssueType) -> list[str]:
        return self._facts.analyze(query, issue_type).get("proof_needed", [])[:4]

    def choose_correct_authority(self, issue_type: IssueType) -> str:
        path, _ = self._authority.resolve(issue_type)
        return path

    def choose_next_step(self, issue_type: IssueType) -> str:
        _, step = self._authority.resolve(issue_type)
        return step

    def build_legal_analysis(self, query: str) -> LegalAnalysis:
        """Full structured analysis pipeline."""
        analysis = LegalAnalysis(core_question=query[:200])

        if not query or not query.strip():
            return analysis

        # Step 1: Issue type
        issue_type, conf = self.identify_issue_type(query)
        analysis.issue_type = issue_type
        analysis.confidence = conf

        # Step 2: Key facts from the query
        analysis.key_facts = self.extract_key_facts(query)

        # If the classifier couldn't find an issue, return minimal analysis.
        if issue_type == IssueType.UNKNOWN:
            analysis.safe_summary = (
                "لم يتضح نوع المسألة بدقة — يُرجى توضيح المجال "
                "(عمل / مالي / أسرة / إيجار / إداري / جنائي / إجرائي)."
            )
            return analysis

        # Step 3: Fact quality per issue type
        analysis.supporting_facts = self.identify_supporting_facts(query, issue_type)
        analysis.weakening_facts = self.identify_weakening_facts(query, issue_type)

        # Step 4: Opposing arguments
        analysis.opposing_arguments = self.identify_opposing_arguments(query, issue_type)

        # Step 5: Proof requirements
        analysis.proof_requirements = self.identify_proof_requirements(query, issue_type)

        # Step 6: Authority path + next step
        analysis.authority_path = self.choose_correct_authority(issue_type)
        analysis.next_step = self.choose_next_step(issue_type)

        # Procedural risk (short flag)
        if issue_type in (IssueType.APPEAL_DEADLINE,
                           IssueType.ENFORCEMENT_PROCEDURAL,
                           IssueType.ADMINISTRATIVE_OBJECTION):
            analysis.procedural_risk = "عالٍ — مرتبط بمهل قانونية"
        elif issue_type == IssueType.RENTAL_EVICTION and "ما أرسلت" in query:
            analysis.procedural_risk = "متوسط — ينقص الإنذار الرسمي"
        else:
            analysis.procedural_risk = "متوسط"

        # Safe summary (one line)
        analysis.safe_summary = (
            f"المسألة: {ISSUE_TYPE_AR[issue_type]} — "
            f"تحتاج تحليلاً مفصلاً للوقائع والأدلة."
        )

        log.info("[LEGAL_BRAIN] issue=%s conf=%.2f supp=%d weak=%d opp=%d proof=%d",
                 issue_type.value, conf, len(analysis.supporting_facts),
                 len(analysis.weakening_facts), len(analysis.opposing_arguments),
                 len(analysis.proof_requirements))
        return analysis


# ══════════════════════════════════════════════════════════════
# Activation Gate
# ══════════════════════════════════════════════════════════════

def should_activate_legal_thinking(query: str) -> bool:
    """
    Decides whether a query merits full legal analysis.
    True for complex personal litigation queries, False for simple info queries.
    """
    if not query:
        return False
    n_words = len(query.split())
    # Very short queries are usually info queries — skip
    if n_words < 8:
        return False
    # Must have a consultation trigger
    if not any(t in query for t in _CONSULTATION_TRIGGERS):
        # But also accept if the query clearly describes a personal legal situation
        # (multiple issue-type keywords)
        classifier = IssueClassifier()
        issue, conf = classifier.classify(query)
        return issue != IssueType.UNKNOWN and conf >= 0.5
    return True


# ══════════════════════════════════════════════════════════════
# User-Facing Formatter
# ══════════════════════════════════════════════════════════════

def format_legal_analysis(analysis: LegalAnalysis) -> str:
    """Format a LegalAnalysis as Arabic user-facing text with clear sections."""
    if not analysis.is_substantive():
        return ""

    parts = []

    # Header: issue type
    parts.append(f"📌 نوع المسألة: {ISSUE_TYPE_AR[analysis.issue_type]}")

    # Supporting facts
    if analysis.supporting_facts:
        parts.append("\n**ما يقوي موقفك:**")
        for s in analysis.supporting_facts[:4]:
            parts.append(f"• {s}")

    # Weakening facts
    if analysis.weakening_facts:
        parts.append("\n**ما قد يُضعف موقفك:**")
        for w in analysis.weakening_facts[:4]:
            parts.append(f"• {w}")

    # Opposing arguments
    if analysis.opposing_arguments:
        parts.append("\n**ما قد يحتج به الطرف الآخر:**")
        for o in analysis.opposing_arguments[:4]:
            parts.append(f"• {o}")

    # Proof requirements
    if analysis.proof_requirements:
        parts.append("\n**ما يحتاج إثبات أو استكمال:**")
        for p in analysis.proof_requirements[:4]:
            parts.append(f"• {p}")

    # Next step (most important for the user)
    if analysis.next_step:
        parts.append("\n**الخطوة العملية التالية:**")
        parts.append(f"• {analysis.next_step}")

    # Authority path
    if analysis.authority_path:
        parts.append("\n**الجهة المختصة:**")
        parts.append(f"• {analysis.authority_path}")

    return "\n".join(parts)


