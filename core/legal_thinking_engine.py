class IssueClassifier:
    """Classifies a query into one of 9 canonical issue types."""

    def classify(self, query: str) -> tuple[IssueType, float]:
        """Returns (issue_type, confidence). Confidence 0-1."""
        if not query:
            return IssueType.UNKNOWN, 0.0

        scores: dict[IssueType, int] = {}
        for issue, signals in _ISSUE_SIGNALS.items():
            score = sum(w for kw, w in signals if kw in query)
            if score > 0:
                scores[issue] = score

        if not scores:
            return IssueType.UNKNOWN, 0.0

        best = max(scores, key=scores.get)
        best_score = scores[best]
        # Confidence: normalized score
        conf = min(1.0, best_score / 6.0)
        return best, conf


