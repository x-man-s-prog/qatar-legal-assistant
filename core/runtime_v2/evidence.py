# ─────────────────────────────────────────────────────────────────────
# Public API
# ─────────────────────────────────────────────────────────────────────

def retrieve_evidence(domain: DomainRules) -> list[EvidenceItem]:
    """Return the canonical evidence bound to a domain. No external
    retrieval, no chunk store access."""
    return list(_BANK.get(domain.key, ()))


def verify_canonical(items: list[EvidenceItem]) -> list[EvidenceItem]:
    """Canonical verification gate. Drops anything whose `is_verified`
    flag is not True. Since the bank is hand-curated, this is an
    explicit named step that leaves the list unchanged."""
    return [e for e in items if e.is_verified]
