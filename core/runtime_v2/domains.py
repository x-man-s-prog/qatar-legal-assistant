# ─────────────────────────────────────────────────────────────────────
# Arabic normalization — used both for domain resolution and for fact
# marker matching. Keep this identical across the runtime.
# ─────────────────────────────────────────────────────────────────────

_AR_DIAC = "\u064b\u064c\u064d\u064e\u064f\u0650\u0651\u0652\u0670"


def ar_norm(s: str) -> str:
    t = (s or "").lower()
    for ch in _AR_DIAC:
        t = t.replace(ch, "")
    t = (t.replace("أ", "ا").replace("إ", "ا").replace("آ", "ا")
           .replace("ى", "ي").replace("ة", "ه"))
    return t


