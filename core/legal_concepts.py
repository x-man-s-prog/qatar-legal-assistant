def _normalize(text: str) -> str:
    """توحيد الألف وإزالة التشكيل للمطابقة المرنة."""
    if not text:
        return ""
    # إزالة التشكيل
    text = re.sub(r"[\u064B-\u0652\u0670\u0640]", "", text)
    # توحيد الألف
    text = text.replace("أ", "ا").replace("إ", "ا").replace("آ", "ا")
    # توحيد التاء المربوطة
    text = text.replace("ة", "ه")
    # توحيد الياء
    text = text.replace("ى", "ي")
    return text.strip()


def find_concepts_in_query(query: str) -> List[Dict[str, object]]:
    """
    يبحث عن المفاهيم القانونية الواردة في السؤال ويُرجع تعريفاتها.

    المطابقة مرنة: تتجاهل التشكيل وتوحّد الألف والياء.
    """
    if not query:
        return []

    q_norm = _normalize(query)
    found: List[Dict[str, object]] = []
    seen: set = set()

    for canonical, meta in LEGAL_CONCEPTS.items():
        if canonical in seen:
            continue

        # اختبر المصطلح الأساسي
        if _normalize(canonical) in q_norm:
            found.append({"term": canonical, **meta})
            seen.add(canonical)
            continue

        # اختبر variants
        variants = _CONCEPT_VARIANTS.get(canonical, [])
        for v in variants:
            if _normalize(v) in q_norm:
                found.append({"term": canonical, **meta})
                seen.add(canonical)
                break

    return found


