_NEGATION_MARKERS = ("ليس", "ليست", "غير", "ما هو", "لا يُعد", "لا يعد", "خلافاً", "بخلاف")


def _is_negated_match(answer: str, match_start: int, match_end: int) -> bool:
    """
    يفحص ما إذا كانت المطابقة مسبوقة بأداة نفي أو نفي داخلي (كـ"ليس")،
    كي نتجنّب false positives من نوع «الخلع ليس طلاقاً رجعياً».
    """
    window = answer[max(0, match_start - 60): match_end + 5]
    return any(neg in window for neg in _NEGATION_MARKERS)


def verify_concepts_in_answer(
    answer: str,
    provided_concepts: List[str],
) -> List[str]:
    """
    يتحقق من أن الإجابة لا تتناقض مع التعريفات المقدّمة.
    يُرجع قائمة بالتنبيهات إن وُجدت هلوسات معروفة.

    provided_concepts: قائمة بأسماء المفاهيم التي حُقنت تعريفاتها في السياق.
    """
    if not answer:
        return []

    warnings: List[str] = []
    for term, pattern, correction in _KNOWN_HALLUCINATIONS:
        # تحقق فقط للمفاهيم ذات الصلة بالسؤال
        if provided_concepts and term not in provided_concepts:
            # لكن دعنا نتحقق أيضاً إن ورد المصطلح في الإجابة نفسها
            if term not in answer:
                continue

        m = re.search(pattern, answer)
        if not m:
            continue
        # تجاهل إذا كانت المطابقة مسبوقة بنفي صريح
        if _is_negated_match(answer, m.start(), m.end()):
            continue
        warnings.append(correction)

    # إزالة المكررات مع الحفاظ على الترتيب
    seen = set()
    out: List[str] = []
    for w in warnings:
        if w not in seen:
            seen.add(w)
            out.append(w)
    return out


# ═══════════════════════════════════════════════════════════════════════════
# التحقق من أرقام المواد المذكورة في الإجابة
# ═══════════════════════════════════════════════════════════════════════════

# يدعم كل الأشكال: المادة 317 / مادة 317 / الماده (317) / المادة رقم 317 /
# م317 / م 317 / م-317. الـ regex يُعيد مجموعات متعددة — نأخذ أول مجموعة
# غير فارغة في كل مطابقة عبر findall + فلترة في الدالة.
_ARTICLE_RE = re.compile(
    r"(?:ال)?ماد[ةه]\s*(?:رقم\s*)?\(?\s*(\d{1,4})\s*\)?"
    r"|"
    r"\bم\s*[-ـ]?\s*(\d{1,4})\b",
    re.UNICODE,
)


def extract_article_numbers(text: str) -> List[str]:
    """يستخرج أرقام المواد المذكورة في النص.

    يدعم: المادة 317، مادة 317، الماده (317)، المادة رقم 317،
    م317، م 317، م-317.
    """
    if not text:
        return []
    out: List[str] = []
    for m in _ARTICLE_RE.finditer(text):
        # كل نمط يحمل رقماً في مجموعة مختلفة — خذ أول غير فارغ
        for g in m.groups():
            if g:
                out.append(g)
                break
    return out


def verify_articles_in_context(
    answer: str,
    context_text: str,
    tolerance: int = 0,
) -> List[str]:
    """
    يتحقق من أن كل رقم مادة مذكور في الإجابة موجود في السياق المقدّم.
    يُرجع قائمة بأرقام المواد التي ذُكرت في الإجابة ولم تظهر في السياق.

    tolerance=0 يعني المطابقة الصارمة؛ قيمة أعلى تسمح بفرق في الرقم.
    """
    if not answer or not context_text:
        return []

    answer_arts = set(extract_article_numbers(answer))
    context_arts = set(extract_article_numbers(context_text))

    if not answer_arts:
        return []

    suspicious: List[str] = []
    for a in answer_arts:
        if a in context_arts:
            continue
        if tolerance > 0:
            try:
                n = int(a)
                if any(abs(n - int(c)) <= tolerance for c in context_arts if c.isdigit()):
                    continue
            except ValueError:
                pass
        suspicious.append(a)
    return suspicious


