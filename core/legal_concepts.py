# ═══════════════════════════════════════════════════════════════════════════

def build_concept_context(concepts: List[Dict[str, object]]) -> str:
    """
    يبني نصاً يُحقن في system prompt لتثبيت التعريفات الدقيقة.
    الهدف: منع GPT من اختراع تعريفات من ذاكرته للقانون المصري/السعودي.
    """
    if not concepts:
        return ""

    lines = ["═══ تعريفات قانونية قطرية ملزمة (التزم بها حرفياً) ═══"]
    for c in concepts:
        term = c.get("term", "")
        definition = c.get("definition", "")
        source = c.get("source", "")
        not_to_confuse = c.get("not_to_confuse_with", [])

        lines.append(f"\n▪ {term}:")
        lines.append(f"  {definition}")
        if source:
            lines.append(f"  — المصدر: {source}")
        if not_to_confuse:
            lines.append(f"  ⚠ لا تخلط مع: {' | '.join(not_to_confuse)}")

    lines.append(
        "\n⛔ إذا ورد أحد هذه المصطلحات في سؤال المستخدم — "
        "استخدم التعريف أعلاه حرفياً ولا تخترع تعريفاً من ذاكرتك."
    )
    return "\n".join(lines)


