# ════════════════════════════════════════════════════════════
# نسب نجاح أساسية حسب الموضوع (من تحليل أحكام التمييز)
# ════════════════════════════════════════════════════════════

_BASE_RATES = {
    "مخدرات": 35,
    "حضانة": 65,
    "ضرب": 45,
    "فصل_عمل": 60,
    "شيك": 40,
    "تشهير": 55,
    "سرقة": 30,
    "احتيال": 40,
    "تزوير": 35,
    "ابتزاز": 50,
    "طلاق": 70,
    "نفقة": 75,
}

# ════════════════════════════════════════════════════════════
# عوامل تعديل النسبة
# ════════════════════════════════════════════════════════════

_POSITIVE_FACTORS = {
    # أدلة قوية
    "فيديو": (15, "فيديو/كاميرا كدليل"),
    "كاميرا": (15, "تسجيل مرئي"),
    "تقرير طبي": (10, "تقرير طبي رسمي"),
    "عقد": (8, "عقد مكتوب"),
    "شهود": (8, "شهود عيان"),
    "واتساب": (5, "محادثات إلكترونية"),
    "رسائل": (5, "رسائل/مراسلات"),
    # ظروف مخففة
    "أول مرة": (10, "أول مرة — ظرف مخفف"),
    "أول مره": (10, "أول مرة — ظرف مخفف"),
    # إجرائية
    "بدون إذن": (15, "تفتيش بدون إذن — بطلان محتمل"),
    "بدون اذن": (15, "تفتيش بدون إذن — بطلان محتمل"),
    "دفاع شرعي": (12, "دفاع عن النفس"),
    "دفاع عن نفسي": (12, "دفاع شرعي"),
    "دافعت عن نفسي": (12, "دفاع شرعي"),
    # حضانة
    "تزوجت من أجنبي": (20, "م168 — زواج من أجنبي يسقط الحضانة"),
    "أجنبي": (15, "زوج أجنبي عن المحضون"),
    "مهملة": (10, "إهمال الحاضنة"),
    # عمل
    "بدون سبب": (12, "فصل بدون سبب مشروع"),
    "بدون إنذار": (10, "فصل بدون إنذار مسبق"),
}

_NEGATIVE_FACTORS = {
    "سوابق": (-15, "سوابق جنائية"),
    "يعترف بالحيازة": (-10, "اعتراف بالفعل"),
    "اعترفت": (-10, "اعتراف"),
    "كمية كبيرة": (-12, "كمية كبيرة — اتجار محتمل"),
    "بناء على إذن": (-5, "وجود إذن تفتيش سليم"),
}


def calculate_case_strength(topic: str, query: str, user_facts: dict) -> dict:
    """
    يحسب قوة القضية كنسبة مئوية مع تفصيل العوامل.
    """
    base = _BASE_RATES.get(topic, 50)
    factors = []
    q_low = (query or "").lower()
    facts_text = " ".join(str(x) for x in user_facts.get("confirmed", []) + user_facts.get("challengeable", []))
    all_text = q_low + " " + facts_text

    # عوامل إيجابية
    for keyword, (bonus, label) in _POSITIVE_FACTORS.items():
        if keyword in all_text:
            base += bonus
            factors.append(f"+{bonus}% {label}")

    # عوامل سلبية
    for keyword, (penalty, label) in _NEGATIVE_FACTORS.items():
        if keyword in all_text:
            base += penalty
            factors.append(f"{penalty}% {label}")

    # ثغرات إجرائية
    if "تحريات" in facts_text or "تجاوز حدود" in facts_text:
        base += 12
        factors.append("+12% ثغرات إجرائية محتملة")

    # حدود
    base = max(5, min(95, base))

    # التوصية
    if base >= 70:
        rec = "موقفك قوي — ننصح بالمضي في الدعوى بثقة"
    elif base >= 45:
        rec = "موقفك متوسط — فكّر في التسوية كخيار بديل مع المضي في الدعوى"
    else:
        rec = "موقفك ضعيف — ركّز على الظروف المخففة والتفاوض"

    return {
        "overall": round(base),
        "factors": factors,
        "recommendation": rec,
    }


def format_strength_report(strength: dict) -> str:
    """يُنسّق تقرير القوة للعرض."""
    text = f"\n📊 تقييم قوة القضية: {strength['overall']}%\n"
    if strength["factors"]:
        text += "العوامل المؤثرة:\n"
        for f in strength["factors"]:
            text += f"  • {f}\n"
    text += f"\n💡 {strength['recommendation']}\n"
    return text
