# يكتشف: المادة 357، المادة (357)، م.357، م 357، م/357، مادة 357
_ART_RE = re.compile(
    r'(?:'
    r'(?:المادة|مادة)\s*[\(]?\s*(\d{1,3})\s*[\)]?'  # المادة 357 أو المادة (357)
    r'|'
    r'م\s*[\.\/]\s*(\d{1,3})'                         # م.357 أو م/357
    r'|'
    r'م\s+(\d{1,3})(?!\d)'                             # م 357
    r')',
    re.UNICODE
)


def _detect_topic(query: str) -> Optional[str]:
    """اكتشاف موضوع السؤال من الكلمات المفتاحية"""
    q = query.lower()
    best = None
    best_count = 0
    for topic, keywords in _TOPIC_KEYWORDS.items():
        count = sum(1 for kw in keywords if kw in q)
        if count > best_count:
            best_count = count
            best = topic
    return best


def _get_allowed_articles(topic: str) -> set:
    """استخراج كل المواد المسموحة لموضوع معين"""
    allowed_laws = _TOPIC_LAWS.get(topic, [])
    allowed = set()
    for law_key in allowed_laws:
        law_data = _VALID_ARTICLES.get(law_key)
        if law_data:
            allowed |= law_data["articles"]
    # إضافة مواد متحقق منها خارج DB
    extra = _TOPIC_EXTRA_ARTICLES.get(topic, set())
    allowed |= extra
    return allowed


def _get_law_name(topic: str) -> str:
    """اسم القانون الرئيسي لموضوع معين"""
    allowed_laws = _TOPIC_LAWS.get(topic, [])
    if allowed_laws:
        law_data = _VALID_ARTICLES.get(allowed_laws[0])
        if law_data:
            return law_data["law"]
    return "القانون القطري"


def validate_and_fix_articles(text: str, query: str) -> str:
    """
    فحص النص النهائي وإصلاح أرقام المواد المختلقة.

    Args:
        text: النص المولّد من النموذج (الرد الكامل)
        query: سؤال المستخدم الأصلي (لاكتشاف الموضوع)

    Returns:
        النص بعد الإصلاح (أو نفس النص إذا لم يُكتشف خطأ)
    """
    topic = _detect_topic(query)
    if not topic:
        # لم نتمكن من تحديد الموضوع — لا نتدخل
        return text

    allowed = _get_allowed_articles(topic)
    if not allowed:
        return text

    law_name = _get_law_name(topic)
    fixes = []

    # البحث عن كل أرقام المواد في النص
    for m in _ART_RE.finditer(text):
        # أحد المجموعات الثلاث ستحتوي الرقم
        art_str = m.group(1) or m.group(2) or m.group(3)
        if not art_str:
            continue
        art_num = int(art_str)

        # تحقق: هل هذه المادة من القانون الخطأ؟
        is_known_contamination = (topic, art_num) in _CROSS_CONTAMINATION
        is_not_in_allowed = art_num not in allowed

        # المادة 49 في قانون العقوبات (دفاع شرعي) مسموحة في الضرب
        # حتى لو ليست في الموضوعات الأخرى
        if topic == "ضرب" and art_num == 49:
            continue

        if is_known_contamination or is_not_in_allowed:
            original = m.group(0)
            # بدلاً من ذكر رقم خاطئ، نضع اسم القانون
            replacement = f"(وفقاً لأحكام {law_name})"
            fixes.append({
                "original": original,
                "art_num": art_num,
                "replacement": replacement,
                "reason": _CROSS_CONTAMINATION.get((topic, art_num), f"المادة {art_num} غير موجودة في {law_name}"),
                "pos": m.start(),
            })

    if not fixes:
        return text

    # تطبيق الإصلاحات من النهاية للبداية (حتى لا تتأثر المواضع)
    result = text
    for fix in sorted(fixes, key=lambda f: f["pos"], reverse=True):
        result = result[:fix["pos"]] + fix["replacement"] + result[fix["pos"] + len(fix["original"]):]

    n = len(fixes)
    _fix_arts = [str(f["art_num"]) for f in fixes]
    log.info(f"[ArticleValidator] topic={topic} | fixes={n} | arts={_fix_arts}")

    return result


def validate_articles_report(text: str, query: str) -> dict:
    """
    تقرير تفصيلي عن المواد في النص (للاختبار والمراقبة).

    Returns:
        dict: {"topic", "total_refs", "valid", "invalid", "fixes": [...]}
    """
    topic = _detect_topic(query)
    report = {
        "topic": topic,
        "total_refs": 0,
        "valid": 0,
        "invalid": 0,
        "fixes": [],
    }
    if not topic:
        return report

    allowed = _get_allowed_articles(topic)

    for m in _ART_RE.finditer(text):
        art_str = m.group(1) or m.group(2) or m.group(3)
        if not art_str:
            continue
        art_num = int(art_str)
        report["total_refs"] += 1

        if topic == "ضرب" and art_num == 49:
            report["valid"] += 1
            continue

        is_bad = (topic, art_num) in _CROSS_CONTAMINATION or art_num not in allowed
        if is_bad:
            report["invalid"] += 1
            report["fixes"].append({
                "article": art_num,
                "text": m.group(0),
                "reason": _CROSS_CONTAMINATION.get((topic, art_num), f"غير موجودة في القانون"),
            })
        else:
            report["valid"] += 1

    return report
