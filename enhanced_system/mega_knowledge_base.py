# محرك الفهم اللغوي الضخم
# ═══════════════════════════════════════════════════════════════════════════════════════

class MegaLinguisticEngine:
    """محرك الفهم اللغوي الضخم"""

    def __init__(self):
        self.dictionary = MegaLegalDictionary()
        self.patterns = MegaQueryPatterns()
        self.contexts = MegaLegalContexts()

        # بناء فهرس المصطلحات
        self._build_term_index()

        # بناء فهرس الأنماط
        self._build_pattern_index()

        # بناء فهرس السياق
        self._build_context_index()

    def _build_term_index(self):
        """بناء فهرس المصطلحات للبحث السريع"""
        self.term_index = {}

        # إضافة المصطلحات الأساسية
        for term, data in self.dictionary.CORE_TERMS.items():
            for variant in data.get("variants", []):
                self.term_index[variant.lower()] = {
                    "term": term,
                    "type": "core",
                    "data": data
                }

        # إضافة المصطلحات الموسعة
        for term in self.dictionary.INTERNATIONAL_LAW_TERMS:
            self.term_index[term.lower()] = {
                "term": term,
                "type": "extended",
                "data": None
            }

    def _build_pattern_index(self):
        """بناء فهرس أنماط الأسئلة"""
        self.pattern_index = {}

        for category, data in self.patterns.QATARI_LAW_PATTERNS.items():
            for question in data.get("questions", []):
                words = self._extract_keywords(question)
                for word in words:
                    if word not in self.pattern_index:
                        self.pattern_index[word] = []
                    self.pattern_index[word].append({
                        "question": question,
                        "category": category,
                        "data": data
                    })

    def _build_context_index(self):
        """بناء فهرس السياقات"""
        self.context_index = {}

        for context, items in self.contexts.CONTEXTS.items():
            for item in items:
                words = self._extract_keywords(item)
                for word in words:
                    if word not in self.context_index:
                        self.context_index[word] = []
                    self.context_index[word].append({
                        "context": context,
                        "item": item
                    })

    def _extract_keywords(self, text: str) -> List[str]:
        """استخراج الكلمات المفتاحية"""
        stop_words = {
            "في", "من", "على", "إلى", "عن", "مع", "هذا", "هذه", "ذلك", "تلك",
            "ما", "هو", "هي", "أو", "و", "لكن", "إذا", "هل", "لا", "نعم",
            "كيف", "متى", "أين", "لماذا", "كم", "أي", "بعض", "كل", "غير"
        }

        words = re.findall(r'[\u0600-\u06FF]+', text.lower())
        return [w for w in words if w not in stop_words and len(w) > 2]

    def analyze_query(self, query: str) -> Dict:
        """تحليل الاستعلام"""
        words = self._extract_keywords(query)

        # البحث عن المصطلحات القانونية
        legal_terms = []
        for word in words:
            if word in self.term_index:
                legal_terms.append(self.term_index[word])

        # البحث عن أنماط الأسئلة المشابهة
        similar_questions = []
        for word in words:
            if word in self.pattern_index:
                similar_questions.extend(self.pattern_index[word][:3])

        # البحث عن السياقات ذات الصلة
        related_contexts = []
        for word in words:
            if word in self.context_index:
                related_contexts.extend(self.context_index[word][:2])

        # تحديد المجال القانوني
        domain = self._determine_domain(legal_terms, similar_questions)

        # تحديد نية المستخدم
        intent = self._determine_intent(query)

        # تحديد اللهجة
        dialect = self._detect_dialect(query)

        return {
            "query": query,
            "legal_terms": legal_terms,
            "similar_questions": similar_questions,
            "related_contexts": related_contexts,
            "domain": domain,
            "intent": intent,
            "dialect": dialect,
            "keywords": words,
            "confidence": self._calculate_confidence(legal_terms, similar_questions)
        }

    def _determine_domain(self, terms: List, patterns: List) -> str:
        """تحديد المجال القانوني"""
        if not terms and not patterns:
            return "unknown"

        domain_scores = defaultdict(int)

        for term in terms:
            term_text = term.get("term", "").lower()
            for domain, contexts in self.contexts.CONTEXTS.items():
                for context in contexts:
                    if term_text in context.lower():
                        domain_scores[domain] += 1

        for pattern in patterns:
            category = pattern.get("category", "")
            if category:
                domain_scores[category] += 2

        if domain_scores:
            return max(domain_scores, key=domain_scores.get)

        return "unknown"

    def _determine_intent(self, query: str) -> str:
        """تحديد نية المستخدم"""
        query_lower = query.lower()

        intents = {
            "استفسار": ["ما هي", "ما حكم", "ما عقوبة", "ما شروط", "ما إجراءات", "كيف", "متى", "هل يمكن"],
            "إجراء": ["كيف أسوي", "كيف أقدر", "ما المطلوب", "وين أروح", "كيف يتم", "ما الخطوات"],
            "شكوى": ["أشكو", "مظلوم", "تظلم", "مشكلة", "نزاع", "خلاف"],
            "تأمين": ["حماية", "أمان", "ضمان", "تكافل"],
            "إنفاذ": ["نفذ", "طالب", "أريد", "أحتاج"],
        }

        for intent, keywords in intents.items():
            if any(kw in query_lower for kw in keywords):
                return intent

        return "استفسار"

    def _detect_dialect(self, query: str) -> str:
        """كشف اللهجة"""
        for dialect, data in self.patterns.DIALECT_PATTERNS.items():
            for greeting in data.get("greetings", []):
                if greeting in query:
                    return dialect

        return "فصحى"

    def _calculate_confidence(self, terms: List, patterns: List) -> float:
        """حساب مستوى الثقة"""
        base_confidence = 0.3

        if terms:
            base_confidence += min(len(terms) * 0.1, 0.3)

        if patterns:
            base_confidence += min(len(patterns) * 0.1, 0.3)

        return min(base_confidence, 0.95)

    def get_expanded_query(self, query: str) -> List[str]:
        """توسيع الاستعلام"""
        analysis = self.analyze_query(query)

        expanded = [query]

        # إضافة الاستعلامات المشابهة
        for pattern in analysis.get("similar_questions", [])[:5]:
            question = pattern.get("question", "")
            if question and question != query:
                expanded.append(question)

        # إضافة الاستعلامات باستخدام المصطلحات ذات الصلة
        for term in analysis.get("legal_terms", [])[:3]:
            term_text = term.get("term", "")
            if term_text:
                expanded.append(f"ما هي عقوبة {term_text} في قطر؟")
                expanded.append(f"كيف يتم التعامل مع {term_text}؟")

        return list(set(expanded))[:10]


