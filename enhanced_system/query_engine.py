            'في', 'من', 'إلى', 'على', 'عن', 'ب', 'ل', 'ك',
            'هو', 'هي', 'هم', 'هن', 'أنا', 'أنت', 'أنتِ', 'نحن', 'أنتم', 'أنتن',
            'هذا', 'هذه', 'هذي', 'ذلك', 'تلك', 'هلا', 'اهلا', 'مرحبا', 'السلام',
            'لو', 'إذا', 'اذا', 'لأن', ' لان', 'لكن', 'بل', 'او', 'أو', 'ع',
            'مع', 'بعد', 'قبل', 'بين', 'منذ', 'حتى', 'خلال', 'حول', 'ضد',
            'يا', 'أ', 'ب', 'ت', 'ن', 'ل', 'ف', 'و', 'س', 'لم', 'ما', 'لا',
            'لماذا', 'لأن', 'كي', 'حتى', 'إذن', 'أذن', 'كل', 'بعض', 'أي',
