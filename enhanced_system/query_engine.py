    def _normalize(self, text: str) -> str:
        """تطبيع النص للمقارنة الحساسة"""
        text = re.sub(r'[\u064B-\u065F\u0670]', '', text)  # حذف التشكيل والهمزات
        text = re.sub(r'[أإآٱ]', 'ا', text)                 # توحيد الألف
        text = re.sub(r'[ة]', 'ه', text)                     # ة → ه في الآخر
        text = re.sub(r'[ى]', 'ي', text)                     # ى → ي
        text = re.sub(r'[ؤئ]', 'ء', text)                    # تصحيح
        return text.lower().strip()

    def _normalize_preserve_hamza(self, text: str) -> str:
        """تطبيع مع الحفاظ على الهمزة للعرض"""
        text = re.sub(r'[\u064B-\u065F\u0670]', '', text)
