def send_stream(query, session="test"):
    data = json.dumps({"query": query, "model": "openai", "session_id": session}).encode()
    req = urllib.request.Request(API, data=data, headers=HEADERS, method="POST")
    try:
        resp = urllib.request.urlopen(req, timeout=180)
        full = resp.read().decode("utf-8", errors="ignore")
        answer = ""
        for line in full.split("\n"):
            if line.startswith("data: "):
                try:
                    d = json.loads(line[6:])
                    if d.get("type") == "chunk":
                        answer += d.get("text", "")
                except:
                    pass
        return answer.strip()
    except Exception as e:
        return f"ERROR: {e}"

def evaluate_memo(text, query, topic):
    scores = {}
    words = len(text.split())
    scores["الطول"] = min(10, words // 50)
    scores["البسملة"] = 10 if "بسم الله" in text[:150] else 0
    is_cass = "طعن" in query and "تمييز" in query
    is_def = "دفاع" in query or "متهم" in query
    if is_cass:
        scores["المصطلح"] = 10 if "الطاعن" in text else 5
    elif is_def:
        scores["المصطلح"] = 10 if ("المتهم" in text or "المدعى عليه" in text) and "الطاعن" not in text else (0 if "الطاعن" in text else 5)
    else:
        scores["المصطلح"] = 10 if ("المدعي" in text or "المدعى" in text) and "الطاعن" not in text else (0 if "الطاعن" in text else 5)

    if not is_cass:
        scores["لا_لما_كان"] = 0 if "لما كان ذلك وكان الحكم" in text[:300] else 10
    else:
        scores["لا_لما_كان"] = 10

    articles = re.findall(r'المادة\s*\d+', text)
    scores["مواد_قانونية"] = min(10, len(articles) * 2)
    fakes = re.findall(r'm\d+|M\d+', text)
    scores["لا_mXXX"] = 10 if not fakes else 0
    human_words = ["مصلحة","استقرار","رعاية","حماية","أمن","نفسي","عاطفي","إنساني","حرص","قرة عينه","حياته","معاناة","ظلم","حق","كرامة","عدالة"]
    human_count = sum(1 for w in human_words if w in text)
    scores["جانب_إنساني"] = min(10, human_count * 2)
    sections = ["الوقائع","القانوني","الدفوع","الطلبات","أولاً","ثانياً","ثالثاً","رابعاً","الموضوع","الأساس"]
    section_count = sum(1 for s in sections if s in text)
    scores["بنية"] = min(10, section_count * 2)
    scores["لا_ديزل"] = 0 if "ديزل" in text or "نقل مواد خطرة" in text else 10
    full_art = bool(re.search(r'تنص\s+المادة|نصت\s+المادة|نص\s+المادة|المادة\s+\d+\s+(?:من|التي|والتي|على\s+أن)', text))
    scores["نص_المادة"] = 10 if full_art else 3
    total = sum(scores.values())
    return scores, total

