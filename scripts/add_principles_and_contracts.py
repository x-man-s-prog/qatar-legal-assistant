async def main():
    import asyncpg
    conn = await asyncpg.connect(DB_DSN)
    print("Connected.\n")

    pre = await conn.fetchval("SELECT count(*) FROM chunks WHERE is_active=true")
    print(f"BEFORE: {pre:,} active chunks")

    inserted_p = 0
    inserted_c = 0

    # Insert principles
    print(f"\n--- Inserting {len(PRINCIPLES)} judicial principles ---")
    for p in PRINCIPLES:
        existing = await conn.fetchval(
            "SELECT count(*) FROM chunks WHERE law_name=$1 AND article_number=$2 AND is_active=true",
            p["law_name"], p["article"]
        )
        if existing > 0:
            continue
        emb = get_embedding(p["content"])
        if emb:
            await conn.execute("""
                INSERT INTO chunks (law_name, law_number, law_year, article_number, content, embedding, domain, source, is_active)
                VALUES ($1, $2, $3, $4, $5, $6::vector, $7, 'almeezan', true)
            """, p["law_name"], p["number"], p["year"], p["article"], p["content"], emb, p["domain"])
            inserted_p += 1
    print(f"  Inserted: {inserted_p} principles")

    # Insert contracts
    print(f"\n--- Inserting {len(CONTRACTS)} contract templates ---")
    for c in CONTRACTS:
        existing = await conn.fetchval(
            "SELECT count(*) FROM chunks WHERE law_name=$1 AND article_number=$2 AND is_active=true",
            c["law_name"], c["article"]
        )
        if existing > 0:
            continue
        emb = get_embedding(c["content"])
        if emb:
            await conn.execute("""
                INSERT INTO chunks (law_name, law_number, law_year, article_number, content, embedding, domain, source, is_active)
                VALUES ($1, $2, $3, $4, $5, $6::vector, $7, 'almeezan', true)
            """, c["law_name"], c["number"], c["year"], c["article"], c["content"], emb, c["domain"])
            inserted_c += 1
    print(f"  Inserted: {inserted_c} contracts")

    post = await conn.fetchval("SELECT count(*) FROM chunks WHERE is_active=true")
    print(f"\nAFTER: {post:,} active chunks (+{post-pre})")
    await conn.close()


